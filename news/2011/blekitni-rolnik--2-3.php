<p class="wprowadzenie">Po dwóch kolejnych porażkach w meczach
kontrolnych Rolnik odniósł pierwsze tej wiosny zwyciestwo. Okazał się
lepszy od drużyny Błekitnych Chabielice na ich boisku, aplikując
rywalowi trzy bramki. Chociaż przy lepszej skuteczności byłoby ono
bardziej okazałe.


<p>W niedzielne popołudnie warunki w jakich Rolnik rozgrywał sparing
były bardzo dobre jak na ta porę roku. Temperatura wynosiła niewiele
poniżej zera i był niewielki wiatr. Chociaż wystepowały niewielkie
problemy z przyczepnością. Spotkanie od początku nie było porywające,
mnożyły się niedokładności z obu stron. Pierwszą sytuację w początkowej
fazie spotkania stworzył sobie Rolnik. Po błędzie bramkarza piłkę
przejął <b>Damian Rydz</b> i znalazł się przed pustą bramką. Mimo
ostrego kąta była to szansa stuprocentowa. Niestety nasz zawodnik jej
niewykorzystał. Piłka przeleciała wzdłuż lini bramkowej i wyszła poza
boisko. Szkoda że żaden z zawodników nie zamykał tej akcji. Ale co się
odwlecze to nie uciecze jak mówi przysłowie. Niedługo później Rolnik
wyszedł na prowadzenie. <b>Damian Lesiak</b> zdecydował się na długie
wybicie piłki przed pole karne rywali. Bramkarz Błękitnych popełnił błąd
i piłka po koźle wpadła do bramki. Ta bramka była zaskoczeniem ponieważ
to drużyna z Chabielic była stroną przeważającą. Mimo zdobycia
prowadzenie Rolnik rozgrywał słabą pierwszą połowe. Zawodnicy mieli
problemy z celnym podaniem oraz przyjęciem piłki nie wspominając o
stwarzaniu sytuacji strzeleckich. Kiepska gra się zemściła. Drużyna
Błekitnych dopięła swego i doprowadziła do remisu. Bramkę zdobył <b>"Kuki"</b>.
Niedługo na strzał z dalszej odległości zdecydował sie gracz gospodarzy,
<b>Paweł Śmietański</b>, i piłka wpadła w górny róg bramki <b>Krzysztofa
Smolarka</b>. Rolnik był w stanie odpowiedziec tylko grożnym strzałem <b>Łukasza
Łypa</b> z dwudziestego metra, jednak piłka przeleciała blisko okienka
bramki gospodarzy. Pierwsza połowa zakończyła się wynikiem 2:1.


<p>Na drugą połowę drużyna Rolnika wyszła z pewnymi korektami w składzie
oraz ustawieniu. Jak się okazało były to trafne posunięcia trenera. W
tej części spotkania to Rolnik był stroną dominującą i stworzył sobie
parę okazji do zmiany niekorzystnego rezultatu. Także chabielice miały
swoje szanse. Jednak to nasza drużyna doprowadziła do wyrównania. Do
odbitej piłkę przed polem karnym przejął <b>Paweł Tokarski</b>, zwiódł
rywala, wpadł w pole po czym pokonał bramkarza Błękitnych. Przez kolejne
minuty zawodnicy ze Skapej nie potrafili sobie stworzyć klarownej
sytuacji bramkowej. Za każdym razem brakowało precyzji przy rozgrywaniu
akcji. W porównaniu do poprzednich spotkań oraz pierwszej połowy
zawodnicy Rolnika zaczęli wreszcie próbować strzałów z dystansu. Prób
było wiele ale zdecydowana wiekszość była niecelną bądź za lekka aby
zaskoczyć bramkarza rywali. Kilkanaście minut przed zakończeniem
spotkania zwycięską bramke zdobył Rolnik. Do wybitej piłki przed polem
karnym dopadł <b>Marcin Kazuba</b> zwiódł rywala i uderzył bez
zastanowienia lewą nogą. Piła wpadła pod poprzeczkę. Rywale od razu
wzięli się za odrabianie strat, zaczęli atakować większą liczbą
zawodników co stwarzało szansę do kontr naszej drużynie. Po jednej z
nich w dogodnej sytuacji sam na sam znalazł się <b>Mateusz Karasiński</b>,
jednak jego uderzenie minęło słupek bramki Błękitnych. Niedługo potem
spotkanie zakończyło się.


<p>Po tym spotkaniu należy pochwalić Rolnika za 3 strzelone bramki oraz
grę w drugiej połowie. Natomiast o pierwszej połowie oraz straconych
bramkach należy jak najszybciej zapomnieć. Momentami gra wyglądała
bardzo dobrze, parę akcji mogło się podobać, zwłaszcza szybkie wymiany
piłki przed polem karnym po których pare razy było groźnie. Pochwalić
nalezy także za liczne próby strzałów, których brakowało w poprzednich
spotkaniach. Oczywiście na treningach trzeba popracować nad ich
celnością. Trener próbował zawodników na róźnych pozycjach co raz
przynosiło dobre efekty czasami gorsze. Miejmy nadzieję że niedzielne
spotkanie jest dobrym prognostykiem na kolejne mecze sparingowe oraz
rozgrywki ligowe. Już za tydzień kolejny sparing, tym razem jedziemy do
Dobryszyc na spotkanie z miejscową drużyną.
