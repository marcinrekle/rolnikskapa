<p class="wprowadzenie">W meczu z wyżej notowanym rywalem kolejnej
porażki doznał Rolnik. Pierwsza połowa to przewaga gościu udokumentowana
trzema bramkami. W drugiej troche lepsza gra i bramka dla naszej
drużyny. Honorowe trafienie zaliczył Dawid Taranek.


<p>Mecz rozpoczął się od ataków drużyny gości która od początku
osiągnęła przewagę. Rywal wygrał walkę o środek pola i często
przedostawał się pod pole karne Rolnika. W wyniku tego Zawisza II miała
kilka szans bramkowych, jednak nie były to sytuacje stuprocentowe.
Rolnik próbował kontrować rywali, jednak w akcjach było za wiele
niedokładności. Atakującym gością wreszcie udało się dopiąć swego. Przed
polem karnym faulował zawodnik Rolnika i sędzia podyktował rzut wolny
pośredni. Po krótkim rozegraniu piłki jeden z graczy Zawiszy uderzył
technicznie przy prawym słupku bramki <b>Krzysztofa Smolarka</b> i
goście objęli prowadzenie. Niedługo później przewaga drużyny z Pajęczna
wzrosła do dwóch bramek. Piłkarz Zawiszy wykorzystał sytuację sam na
sam. Trzecia bramka padła po zamieszaniu przed polem karnym z którego
skorzystał jeden z gości i pokonał bramkarza Rolnika w sytuacji sam na
sam. Jeżeli chodzi o sytuację dla Skąpej to warto odnotować szansę <b>Michała
Kapeli</b> i strzał sprzed pola karnego <b>Tomasza Dygudaja</b>. Była
także okazja z rzutu wolnego z lewej strony pola karnego, ale udeżenie <b>Pawła
Tokarskiego</b> przeleciało nad poprzeczką. Po pierwszej połowie Rolnik
miał trzy bramki do odrobienia.


<p>Druga połowa to już lepsza gra Rolnika, który był równorzędnym
rywalem dla Pajęczna. Dobra gra została ukoronowana honorowym trafieniem
autorstwa <b>Dawida Taranka</b>. Nasz zawodnik pokonał bramkarza rywali
w sytuacji sam na sam. Ten gol przywrócił nadzieje na korzystny końcowy
rezultat spotkania. Bardzo dobrą sytuacje na zdobucie kontaktowej bramki
zmarnował <b>Damian Rydz</b>, który po prostopadłym podaniu <b>Konrada
Wojtysiaka</b> stanął oko w oko z golkiperem Zawiszy. Niestety źle
przyjął piłkę. Goście także mieli w tej połowie szansę na kolejne
bramki, ale na nasze szczęście nie potrafili ich wykorzystać. Niestety
bardziej były one przez błędy zawodników Rolnika niż dzięki składnym
akcją. Tak więc wynik już nie uległ zmianie i Rolnik uległ Zawiszy 1:3.







<p>To był kolejny nieudany sparing Rolnika, chociaż częściowo można go
tłumaczyć trudnym rywalem występującym klasę wyżej. Kolejnym powodem są
zapewne ciągłe roszady w składzie i przesuwanie poszczególnych
zawodników na inne pozycje. Miejmy nadzieję że za tydzień bedzię lepiej.
Jest coraz mniej czasu na poprawienie gry. Zostały tylko dwa sparingi.
Rolnik zmierzy się w nich z drużyną Słowiana Dworszowice. <br>



