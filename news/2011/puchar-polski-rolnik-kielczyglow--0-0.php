<p class="wprowadzenie">Na drugiej rundzie swoją przygodę z Pucharem
Polski zakończyłą drużyna Rolnika Skąpa. W trakcie regulaminowych 90
minut oraz dogrywki żadna z drużyn nie była w stanie pokonać bramkarza
rywali. W takim przypadku o awansie decydują rzuty karne. Niestety to
drużyna gości okazała się lepsza w tym elemencie piłkarskiego rzemiosła.
Rywalem w tym spotkaniu był występujący od tego sezonu w A klasie LKS
Kiełczygłów.

<p>Całe spotkanie miało podobny przebieg. Drużyna Rolnika prowadziłą grę
a goście próbowali kontrować. Piłkarze Rolnika często gościli pod polem
karnym Kiełczygłowa i stwarzali sobie sytuacje z których mogły paść
bramki. Niestety brakowało skuteczności i zimnej krwi. Najlepszą
sytuację zmarnował <b>Damian Rydz</b>. Zagraną wzdłuż bramki piłkę
uderzył wprost w ręce golkipera gości. Dwukrotnie po rzutach rożnych w
bardzo dobrej sytuacji znajdował się <b>Seweryn Włodarczyk</b>, jednak
za każdym razem spadającą pod nogi piłkę uderzał obok bramki. W paru
innych sytuacjach piłkarze Rolnika zbyt szybko decydowali się na
strzały. Często zawodziło ostatnie podanie. Jeżeli chodzi o sytuacje
gości to stuprocentowych nie potrafili sobie stworzyć. Najgroźniejsze
było uderzenie ze środka boiska przy rozpoczynaniu drugiej połowy. Był
to bodajże jedyny celny strzał Kiełczygłowa. Oczywiście nie sprawił on
żadnych problemów <b>Radkowi Kurzynoga</b>.

<p>W rzutach karnych lepsi okazali się goście, którzy wykorzystali
wszystkie jedenastki. Niestety nam się ta sztuka nie udała i polegliśmy
4:5. Marzenia o zdobyciu pucharu musimy odłożyć do następnego sezonu ;).







<p>To było kolejne, po meczu ze Strzelcami, dobre w wykonaniu spotkanie
Rolnika. Gdyby była lepsza skuteczność albo było więcej szcześcia
przygoda z pucharem trwałaby nadal. Z przebiegu spotkania Rolnik był
drużyną lepszą ale w piłce nie zawsze lepsi wygrywają. Szkoda że
odpadliśmy ponieważ graliśmy z drużynami z wyższych klas rozgrywkowych a
do tego mecze o stawkę. Wiadomo że takie spotkania są lepszym
przetarciem przed ligą niż mecze sparingowe. <br>
