<p class="wprowadzenie">W przedostatniej kolejce obecnego sezonu Rolnik
grał na wyjeździe z LZS Trębaczew i został rozgromiony. Zapewne kiepsko
będzie wspominał swój debiut bramkarz Rolnika. Dziewięć wpuszczonych
bramek to dużo. Dla naszej drużyny po bramce zdobyli Przemysław
Mielczarek i Damian Rydz.

<p>Od samego początku lepiej prezentowali się gospodarze, którzy dobrze
weszli w mecz. Chociaż nie potrafili osiągnąć dużej przewagi. W drużynie
Rolnika za często zdażały się proste błędy, niecelne zagrania. Jednak z
każdą minutą gra wyglądała coraz lepiej. Wtedy padła pierwsza bramka dla
Trębaczewa. Bardzo lekki strzał zawodnika gospodarzy między rękami
wpuścił debiutujący w Rolniku <b>Paweł Staszak</b>. Taka bramka potrafi
odebrać chęć do gry. Kolejna bramka padła po strzale głową. Golkiper
Rolnika za późno wybiegł do piłki i został uprzedzony przez gracza
gospodarzy. Następna bramka to strzał z rzutu karnego po faulu którego
nie było ponieważ zawodnik Rolnika trafił w piłkę. Czwarta bramka padła
po błędzie obrony. Rolnik był w stanie odpowiedzieć tylko jednym
trafieniem. Po strzale <b>Przemka Mielczarka</b> golkiper gosodarzy nie
dał rady sparować piłki i ta po jego dłoniach wpadła do bramki.



<p>W drugiej połowie gospodarze zdobyli pięć bramek. Większość z nich
padła po kontrach. Rolnik zaatakował większą ilością zawodników co
skrzętnie wykorzystali gracze Trębaczewa. Jedna z bramek padła po
kolejnym rzucie karnym, chociaż w tej sytuacji nie można mieć pretensji
do arbitra. Faul w polu karnym był ewidentny. Bramka dla Rolnika padła
po prostopadłym podaniu <b>Kamila Kapeli</b> do <b>Damiana Rydza</b>.
Nasz zawodnik w sytuacji sam na sam posłał piłke w długi róg obok
wybiegającego bramkarza. Pod koniec spotkania bramkarz Rolnika zgłosił
kontuzję (?), a że rezerwowych już nie było spotkanie kończyliśmy w
dziesiątke. Niedługo potem sędzia zakończył spotkanie.

<p>Napewno nie miło drużyna Rolnika będzie wspominać wyjazd do
Trębaczewa. Zwłaszcza bramkarz który wpuścił aż 9 bramek. Chociaż nie
tylko on był tego dnia słabo dysponowany.


<p>
<p>Już za tydzień ostatni akt sezonu 2010/11 w którym Rolnik na swoim
stadionie podejmie Płomień Makowiska. Zapowiada się ciekawe spotkanie.
Spotkanie rozpocznie się o godzinie 17:00. <br>
