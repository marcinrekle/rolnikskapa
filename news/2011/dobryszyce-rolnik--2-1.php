<p class="wprowadzenie">W kolejnym spotkaniu sparingowy Rolnik dał pokaz
nieskuteczności. Mimo wielu "stuprocentowych" sytuacji zdołał strzelić
tylko jedną bramkę. Do tego doszły błędy w obronie przez które padły
dwie bramki dla rywali. Po pierwszej połowie, w której nasza drużyna
miała dużą przewagę, nikt się nie spodziewał porażki. Jednak druga
połowa była dużo gorsza w naszym wykonaniu.


<p>Mecz rozpoczął się od ataków piłkarzy Skąpej, którzy od poczatku
osiągnęli przewagę. Pierwszą dobrą okazję miał <b>Konrad Wojtysiak</b>,
który przedarł się lewym skrzydłem i oddał strzał na bramkę Dobryszyc.
Niestety golkiper gospodarzy zdołał wybronić uderzenie. Niedługo potem
ten sam zawodnik w podobnej sytuacji uderzył nad bramką. Bardzo dobrze
układała się współpraca obu naszych napastników, co owocowało wieloma
groźnymi sytuacjami podbramkowymi. Niestety szwankowała skuteczność.
Strzały były niecelne, badź dobrze bronił bramkarz LKS Dobryszyce. Pare
razy także w ostatniej chwili udawało się obrońcą rywali powstrzymać
graczy Rolnika. W końcu jedną z sytuacji wykorzystał <b>Damian Rydz</b>
posyłając piłkę obok bramkarza w sytuacji sam na sam. Mimo objęcia
prowadzenia Rolnik nadal atakował i stwarzał zagrożenie pod bramką
rywali. Nawet udało zdobyć się drugą bramkę jednak nie została ona
uznana ponieważ <b>Michał Kapela</b> nie trafił piłkę głową i ta odbiła
mu sie od ręki. Dobrze w tej części spotkania spisywała się nasza obrona
nie dopuszczając do wielu grożnych sytuacji pod naszą bramką. Pierwsza
połowa mimo wielu szans na kolejne gole zakończyła się tylko wynikiem
0:1.


<p>W drugiej połowie obraz gry się zmienił, spotkanie wyrównało się.
Jednak jeżeli chodzi o sytuacje bramkowe nadal przewagę miał Rolnik.
Momentami gra wyglądało bardzo kiepsko, nasi zawodnicy nie potrafili
utrzymać się przy piłce, było wiele strat, niedokładnych podań. Słabiej
zagrała także obrona, która większość piłek wybija na oślep przez co
szybko wracała ona pod nasze pole karne. Niewykorzystane sytuacje szybko
się zemściły. Po dośrodkowaniu z naszej lewej strony niekryty zawodnik
Dobryszyc zdobył głową wyrównującą bramkę. Nawet stracona bramka nie
obudziła zawodników Rolnika, którzy nadal słabo grali. Mimo to potrafili
stworzyć sobie sytuację do odzyskania prowadzenia. Jednak to druzyna
gospodarzy wyszła na prowadzenie. Nieporozumienie stopera i bramkarza
Rolnika wykorzystał napastnik gospodarzy. Do samego końca Rolnik walczył
o korzystny rezultat, ale caly czas zawodziła skuteczność. Spotkanie
zakończyło się jednobramkowym zwycięstwem LKS Dobryszyce.


<p>To spotkanie pokazało dwa oblicza Rolnika. Dobrej drużyny z pierwszej połowy 
,która potrafi zdominować rywala i stworzyc sobie wiele szans bramkowych. Słabej z 
drugiej części gry, popełniającej dużo błędów, zagubionej na boisku. Plusem była ilość stworzonych 
sytuacji, minusem brak ich wykorzystania. Już za tydzień prawdziwy sprawdzian możliwości naszej drużyny. 
Na boisku w Skąpej Rolnik zmierzy się z występującą w A-klasie drugą drużyną Zawiszy Pajęczno. Mecz został
zaplanowany na godzinę 14:30.  

