<p class="wprowadzenie">Dr Jekyll i Mr Hyde tak można określić kolejny
sparing w wykonaniu graczy Rolnika Skąpa. W pierwszej połowie dobra gra,
kontrolowanie sytuacji na boisku, strzelona bramka plus stworzenie
kolejnych sytuacji. Druga całkowite przeciwieństwo pierwszej. Gra nie
szła,bezradność zawodników, rywal przejmuje inicjatywę i strzela pięć
bramek. W każdym spotkaniu przerwa odmienia oblicze Rolnika.




<p>Od początku spotkania to Rolnik sprawiał lepsze wrażenie. Częściej
znajdował się pod polem karnym rywala, stwarzając sobie sytuacje
bramkowe. Zaś rywali do swojej bramki nie dopuszczał. Przewaga została w
końcu udokumentowana bramką <b>Damiana Rydza</b>. Niedługo potem mogła,
a nawet powinna paść kolejna bramka dla Rolnika. W polu karnym
sfaulowany został <b>Konrad Wojtysiak</b>, a sędzia bez wachania
podyktował jedenastke. Do piłki podszedł <b>Paweł Tokarski</b>, jednak
bramkarz rywali nie dał się oszukać i wybronił uderzenie. Jednak to
zdarzenie nie załamało zawodników Rolnika, którzy dalej próbowali zdobyć
kolejne bramki. Słowian także miał rzut karny w pierwszej połowie,
zawodnik gości zahaczył o nogę obrońcy i upadł w polu karnym. W tym
meczu wykonawcy karnych nie mieli dobrego dnia, ponieważ także zawodnik
Słowiana tej szansy nie wykorzystał. Strzał pewnie wybronił <b>Krzysiek
Smolarek</b>. Zawodnicy na przerwe schodzili przy wyniku 1:0 dla
Rolnika.




<p>Na drugą połowe wyszła całkiem inna drużyna Rolnika, i nie chodzi tu
o skład bo zmian w nim nie było, ale o to co działo się na boisku.
Drużyna przestała grać, dała się zdominować przez rywala, już nie
prowadziła gry. Słaba gra w końcu została skarcona. Po rzucie rożnym, po
zamieszaniu w polu karnym silnym strzałem pod poprzeczke goście zdobyli
wyrównująca bramke. Po tej sytuacji drużyna Rolnika "rozsypała" się.
Niedługo potem nastapiły minuty które wstrząsnęły Rolnikiem. W krótkich
odstępach czasu goście zdobyli cztery bramki. Pierwsza była konsekwencją
za dalekiego wysunięcia się przez bramkarza, co wykorzystał zawodnik
Słowiana. Kolejna bramka to błąd w kryciu przy rzucie wolnym i gol po
strzale głową. Czwartą bramke meczu rywale zdobyli po braku zdecydowania
lewego obrońcy i bramkarza co skrzętnie wykorzystali goście. Ostatnia
bramka to strzał z kilku metrów obok bramkarza. Gracze Rolnika próbowali
zmniejszyć rozmiary porażki. Jednak klarownych sytuacji brakowało.
Strzały były niecelne. Warta odnotowania jest sytuacja w której
rozegraniu akcji z pierwszej piłki w polu karnym znalazł sie <b>Konrad
Wojtysiak</b>, jednak jego strzał zdołał obronić golkiper gości. Mimo
prób wynik spotkania nie uległ już zmianie. <br>




<p>Nie lada orzech do rozgryzienia ma trener <b>Jacek Wesołowski</b>. Co zrobić aby Rolnik po dobrej pierwszej połowie, w drugiej także dobrze grał. 
Martwi także brak skuteczności zawodników i seryjnie marnowane sytuacje. Jeżeli chodzi o dzisiejszy mecz jedynym wytłumaczeniem może być ciężka sytuacja kadrowa w tym spotkaniu. 
Trener miał do dyspozycji tylko 12 zawodników. Do rozpoczęcia rundy rewanżowej pozostały tylko 2 tygodnie. W chwili pisania tego newsa nie jest znany jeszcze rywal na ostatni sparing. 
 
