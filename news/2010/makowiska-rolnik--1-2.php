     <p class="wprowadzenie">W ostatniej kolejce rundy jesiennej Rolnik odniósł cenne zwycięstwo na boisku lidera. Mimo że gospodarze prowadzili po pierwszej połowie zawodnicy pokazali charakter i potrafili dwukrotnie pokonać bramkarza rywali. Bramki zdobyli Dawid Taranek i Paweł Tokarski. Nie było to łatwe gdyż mecz prowadzi wszystkim dobrze znany "Czesio", który sędziował dla Makowisk. Ten rezultat pokazuje jak wyrównane są rozgrywki B-klasy w tym sezonie. <br>
     <p>Jak większość poprzednich spotkań Rolnik rozpoczął niemrawo dlatego dosyć szybko drużyna z Makowisk osiągnęła przewagę. Zawodnicy ofensywni nie potrafili utrzymać piłki w przodzie i ta dosyć szybko wracała pod pole karne Rolnika. Jednak pewnie grająca obrona potrafiła zażegnać niebezpieczeństwo. W kilku sytuacjach było też sporo szczęścia, gdyż z piłką zagraną wzdłuż bramki mijali się zawodnicy gospodarzy. Dobra sytuację bramkową miał także Rolnik. Jednak biegnącemu sam na sam z bramkarzem <b>Damianowi Rydzowi</b> przeszkadzali obrońcy rywali i nasz zawodnik strzelił prosto w bramkarza. W stwarzaniu sytuacji bramkowych przeszkadzali nie tylko rywale, a także sędzia, który nie odgwizdał paru fauli przed polem karnym rywali. 
	  Poza wcześniej wspomnianą sytuacją Rolnik nie miał zbyt wiele sytuacji bramkowych. Za to gospodarzą udało się objąć prowadzenie. Zawodnicy Rolnika nie potrafili wybić piłki sprzed pola karnego mimo kilku prób, ta w końcu wylądowała pod nogami zawodnika Płomienia. Ten znajdując się sam na sam strzelił pewnie w krótki róg bramki. Pierwsza połowa zakończyła się jednobramkowym prowadzeniem gospodarzy. 	 	   
	     <p>Na drugą połowę zawodnicy Rolnika wyszli bardzo zmotywowani i od początku zaatakowali. Zepchnęli rywali do obrony, role z pierwszej połowy się odwróciły. Niestety klarownych sytuacji nie było. Mimo to gracze Rolnika wierzyli w zwycięstwo i walczyli do końca. Ich starania zostały w końcu nagrodzone. Po świetnym podaniu na prawe skrzydło od Damiana Rydza bramkę zdobył <b>Dawid Taranek</b>. Będący na fali Rolnik na parę minut przed końcem zdobył zwycięskiego gola. W polu karnym <b> Paweł Tokarski</b> minął rywala i strzelił obok bramkarza gospodarzy. Rywale rzucili się do odrabiania strat jednak czasu było za mało. Jedyne zagrożenia było po rzutach wolnych sprzed pola karnego, które wynikały z niepotrzebnych kłótni piłkarzy Rolnika z arbitrem spotkania.   
	  <p>W meczu w Makowiskach Rolnik wreszcie się przełamał i przywiózł trzy punkty ze spotkania wyjazdowego. Dodatkowo dokonał tego na boisku lidera, który po tym spotkaniu już nim nie jest. Cieszy walka do końca o korzystny rezultat. 	    	   
