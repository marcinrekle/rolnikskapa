<p class="wprowadzenie">Zła passa Rolnika trwa. Trzeci mecz i trzecia
porażka. Tym razem Rolnik na swoim boisku nie podołał Orionowi Olewin.
Wynik 1:4 nie odzwierciedla przebiegu spotkania. To nasza drużyna
prowadziła grę i stwarzała dużo sytuacji(niewykorzystanych) a to rywale
zdobywali bramki. Honorowe trafienie dla Skąpej zaliczył uderzeniem z
rzutu wolnego Marcin Kazuba.

<p id="opis1">Już po kilkunastu minutach pierwszej połowy drużyna
Rolnika mogła a nawet powinna zdobyć trzy bramki. Jak zwykle zawiodła
skuteczność. Goście nie mieli nic do powiedzenia, chociaż starali się
długo rozgrywać piłkę. Jednak nie potrafili się przebić przez obronę
Rolnika. Niespodziewanie to Orion objął prowadzenie. Piłka wrzucona w
pole karne odbiła się od jednego z graczy gości i wylądowała w siatce.
Druga bramka dla rywali padła po uderzeniu sprzed pola karnego. Niedługo
później rywale zdobyli trzeciego gola. W odpowiedzi Rolnik stworzył
sobie kilka wymarzonych wręcz sytuacji. Uderzenia naszych zawodników
były zbyt słabe, niecelne badź intuicyjnie bronił bramkarz Olewina.
Pierwsza połowa zakończyła się wynikiem 0:3.

<p id="opis2">W drugiej połowie ciągle przeważał Rolnik ale tyle dobrych
sytuacji co w pierwszej sobie już nie stworzył. Chociaż remis był w
zasięgu. Powtórzyła się sytuacja z pierwszej odsłony meczu. Czyli
atakował Rolnik a rywale strzelili bramke. W sytuacji sam na sam blisko
obrony był <b>Radek Kurzynoga</b>, ale piłka po odbiciu od naszego
bramkarza wtoczyła się do bramki. Około 15 minut przed końcem odżyły
nadzieje Rolnika. Jeden z naszych zawodników wywalczył rzut wolny około
17 metrów od bramki Oriona. Bramkę strzałem obok muru zdobył <b>Marcin
Kazuba</b>. Blisko szczęścia był <b>Łukasz Łyp</b>, ale piłka po jego
strzale wylądowała na poprzeczce. Dobitka <b>Tomasza Dygudaja</b>
przeleciała nad poprzeczką. W ostatnich minutach Rolnik nie stworzył już
sobie dobrej sytuacji i spotkanie zakończyło się wynikiem 1:4.

<p id="podsumowanie">Podsumowanie mógłbym z czystym sumieniem skopiować
z poprzednich dwóch spotkań Rolnika. Czyli przede wszystkim zawiodła
skuteczność. W dobrych sytuacjach brakuje spokoju i uderzenia są
niecelne bądź zbyt słabe dla bramkarza.


<p id="nastepny">W kolejnym spotkaniu Rolnik zmierzy się z debiutującym
w tym sezonie w B klasie Zagłębiem Lipnik. Mecz zostanie rozegrany 18
września o godz 15:00 w Radoszewicach. <br>
