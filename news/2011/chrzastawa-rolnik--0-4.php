<p class="wprowadzenie">Wynik niedzielnego spotkania przeciwko
Crząstawie nie rzuca na kolana. Skromna zdobycz bramkowa w meczu
przeciwko drużynie, która w prawie każdym poprzednim straciła powyżej
dziesięciu bramek chwały nie przynosi. Niestety już tradycyjnie zawiodła
skuteczność. Na tym spotkaniu pojawili się przedstawiciele portalu
kartofliska.pl oraz Przeglądu Sportowego.

<p>Spotkanie rozpoczęło się od ciągłych ataków Rolnika. Od samego
początku nasza drużyna miała mnóstwo sytuacji bramkowych, ale zawodziła
skuteczność. Przeszkadzało też fatalne boisko. Przed bramką której w
pierwszej połowie broniła Chrząstawa spokojnie dałoby się rozegrać mecz
piłki plażowej. Bardzo trudno było oddać strzał, celnie podać, przyjąć
piłkę. Po kilkunastu nieudanych próbach wreszcie udało się objąć
prowadzenie. Na strzał sprzed pola karnego zdecydował się <b>Seweryn
Włodarczyk</b>, a kiepsko zachował się bramkarz gospodarzy. Prowadzenie
Rolnika stało się faktem. Wszyscy mieli nadzieje że "worek z bramkami
się rozwiązał". Jednak mimo że sytuacji bramkowych nie brakowało w
pierwszej połowie już żadna bramka nie padła. Głownie w tym zasługa
piłkarzy Rolnika, którzy skutecznością nie grzeszyli. Jeżeli chodzi o
sytuacje Chrząstawy to takowych nie było. Ich gra polegała na
wykopywaniu piłki do przodu w kierunku bramki przeciwnika.

<p>Druga połowa meczu także rozpoczęła się od ataków Rolnika, chociaż
przewaga nie była tak duża jak w pierwszej części spotkania. Sytuacje
bramkowe pojawiały się w większości akcji. Jednak do 70-tej minuty
utrzymywał się wynik 0:1. Drugą bramkę dla naszej drużyny strzałem głową
zdobył <b>Tomasz Krawczyk</b>. Trzecia bramka padła po ładnym uderzeniu
z lewej nogi z około 20 metrów. Piłka wpadła w okolicach okienka bramki
gospodarzy. Autorem tego trafienia był <b>Marcin Kazuba</b>. Po tej
bramce dalej trwał festiwal nieskuteczności Rolnika. Parę minut udało
się zdobyć czwartą bramkę. Swoje drugie trafienie w tym spotkaniu
zaliczył <b>Seweryn Włodarczyk</b>. Rolnik miał jeszcze szansę na
podwyższenie wyniku, niestety nie udało się.


<p>Mimo dobycia kolejnych trzech punktów w Chrząstawie nie ma dużych
powodów do radości. Chodzi o rozmiary tego zwycięstwa, które chwały nam
nie przynoszą. Jeżeli spojrzy się na wcześniejsze spotkania tej drużyny
to wynik dwucyfrowy to norma. Jak to w meczach Rolnika jest już
standardem zawiodła skuteczność. Na meczu pojawili się przedstawiciele
"Przeglądu Sportowego", powodem była możliwość stracenia dwusetnej
bramki przez Chrząstawe. Strzelcem tej bramki był Seweryn Włodarczyk.
Jak widać żeby media się zainteresowały drużyną nie jest konieczna gra
na najwyższym poziomie. W piątkowym dodatku do tej gazety znajdzie się
reportaż z tego meczu. Pojawiły się także media elektroniczne. Był to
portal o najgorszych boiskach w naszym kraju <a
	href="http://www.kartofliska.pl">kartofliska.pl</a>. Na ich stronie
można znaleźć materiały z niedzielnego spotkania.
