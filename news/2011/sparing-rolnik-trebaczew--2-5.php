<p class="wprowadzenie">W ostatnim meczu sparingowym przed rozpoczęciem
rundy wiosennej I grupy sieradzkiej B-klasy Rolnik uległ na swoim boisku
drużynie LZS Trębaczew. Osłabieni brakiem bramkarza zawodnicy ze Skąpej
dali sobie strzelić pięć bramek, a sami zdobyli tylko dwie. Strzelcami
byli Paweł Tokarski i Damian Rydz.


<p>Pierwsza połowa spotkania była wyrównana, chociaż z lekkim wskazaniem
na drużynę Rolnika. Obie drużyny zdobyły po dwie bramki. Na prowadzenie
za sprawą <b>Pawła Tokarskiego</b> wyszedł Rolnik. Niedługo później
padła bramka wyrównująca dla Trębaczewa. Następnie to rywale wyszli na
prowadzenie. Do wyrównania doprowadził <b>Damian Rydz</b>, zdobywając
gola w podbramkowym zamieszaniu.


<p>W drugiej połowie bramki zdobywali już tylko goście. Padły ich trzy.
Zawodnicy Rolnika stworzyli sobie kilka sytuacji na zmianę wyniku.
Jednak nie były to te z gatunku stuprocentowych. <br>


<p>W spotkaniu z drużyną LZS Trębaczew Rolnik zagrał bez kontuzjowanego
bramkarza. Dlatego w bramce musiał stać gracz z pola. W drużynie
brakowało także kilku kontuzjowanych zawodników. Zwłaszzca to pierwsze
wpłynęło znacząco na wynik spotkania, chociaż nie można mieć żadnych
pretensji do osoby broniącej dziś dostępu do bramki Rolnika. Napewno
patrząc na grę nasza drużyna nie zasługiwała na tak wysoką porażkę. <br>


<p>Meczem z Trębaczewem Rolnik zakończył przygotowania do rundy
wiosennej. Przed rozpoczęciem treningów drużyna wreszcie znalazła
trenera, został nim <b>Jacek Wesołowski</b>. Dlatego oprócz
przygotowania się do rundy ważne było poznanie przez trenera zawodników.
Tak aby można było na pierwszy mecz wystawić najlepszy skład na jaki
Rolnika stać. W każdym z rozegranych sparingów drużyna grała w innym
składzie, zawodnicy byli próbowani na różnych pozycjach. Dlatego wyniki
spotkań rozegranych podczas przygotowań mogą nie zachwycać. Jednak sama
gra długimi momentami była bardzo dobra, drużyna stwarzała sobie dużo
sytuacji bramkowych, potrafiła zdominować rywala. Dlatego można z
optymizmem patrzeć na zbliżającą się rundę, zawodników Rolnika stąć na
to aby sprawić wiele radości swoim kibicom. Nadzieje na lepszy wynik
zwiększa także fakt że w meczu z Orkanem II trener wreszcie będzie mógł
skorzystać ze wszystkich graczy. Kilku zawodników wraca po kontuzjach.
Podczas przygotowań do Rolnika dołączyło dwóch nowych zawodników, którzy
niewątpliwie wzmocnią drużynę. Dzięki ich przyjściu zwiększyła się
rywalizacja w drużynie, a także trener ma większe pole manewru. Nowi to
<b>Sebastian Mielczarek</b>i <b>Tomasz Krawczyk</b>. <br>
<br>
