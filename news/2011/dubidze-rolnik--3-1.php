<p class="wprowadzenie">Spotkanie w Dubidzach miało dodatkowy smaczek,
związany ze spotkaniem tych drużyn jesienią. Podczas tego meczu doszło
do bijatyki zawodników obu drużyn. Jednak mimo obaw spotkanie
przebiegało w dobrej atmosferze bez żadnych złośliwości z obu stron. Co
do przebiegu samego spotkania wynik nie może satysfakcjonować Rolnika.
Jeżeli zostałaby wykorzystana choćby połowa sytuacji, to i tak mimo
straty trzech bramek nasza drużyny by zwyciężyła. Jedyna bramka dla
Rolnika padła po rzucie karnym wykorzystanym przez Marcina Kazuba.

<p>Początek meczu był wyrównany, obie drużyny się wzajemnie "badały".
Ani jedni ani drudzy nie potrafili osiągnąć zdecydowanej przewagi.
Pierwsi szansę na gola mieli zawodnicy Skąpej, ale strzał <b>Marcina
Kazuby</b> nie zagroził bramkarzowi gości. Za to bramkę udało się
strzelić Dubidzą. Wykonywali oni rzut wolny z dużej odległości. Po
błędzie golkipera Rolnika piłka wpadła do bramki. Rolnik od razu rzucił
się do odrabiania strat. Zaowocowało to kilkoma sytuacjami podbramkowymi
m.in.<b>Damiana Rydza</b> i <b>Łukasza Łypa</b>. Za każdym razem
brakowało zimnej krwi i sytuacje kończyły się szczęśliwie dla Dubidz. A
że niewykorzystane sytuacje się mszczą bramkę zdobyli gospodarze. W
piłkę podczas próby wybicia nie trafił stoper Rolnika i ta doszła to
zawodnika Dubidz, który zdobył bramkę w sytuacji sam na sam. Rolnik był
w stanie tylko odpowiedzieć kilkoma niecelnymi badź słabymi strzałami.
Duży wpływ na grę obu drużyn miał silny wiatr. Trudno było przewidzieć
jak w powietrzu zachowa się piłka.


<p>Druga część spotkania przebiegała już pod dyktando Rolnika, który z
każdą chwilą osiągał coraz większą przewagę i stwarzał sobie mnóstwo
sytuacji bramkowych. Taki przebieg drugiej połowy nie mógł dziwić ze
względu na niekorzystny wynik. Gospodarze w tej części meczu nie
potrafili stworzyć sobie dobrych sytuacji bramkowych. A nasza drużyna
dała koncert nieporadności pod bramką rywali. O braku bramki decydowały:
błędne wybory poszczególnych zawodników, brak szczęscia lub brak
zdecydowania. Piłka dwa razy trafiała w poprzeczkę, a gracze Rolnika nie
byli w stanie jej dobić. Czasami strzał w ostatniej chwili blokowali
obrońcy, badź zawodnik w bramkę poprostu nie trafiał. Dlatego co chwilę
zawodnicy łapali się za głowe nie wierząc że taką szansę można
zmarnować. Jedyną bramkę Rolnik zdobył na 20 minut przed końcem z rzutu
karnego. Ale od początku. Do piłki zagranej w pole karne próbował dojść
<b>Dawid Taranek</b>, ale został powalony przez obrońce. Sędzia bez
zawachania wskazał na wapno. Do piłki podszedł <b>Marcin Kazuba</b> i
strzałem w środek bramki zdobył honorowe trafienie. Niestety z
kontaktowej bramki Rolnik nie cieszył się zbyt długo. Zaczęło się od
błedu obrońcy, który nie był w stanie wybić piłki sprzed pola karnego z
czego skorzystał gracz gospodarzy. Zagrał ją w pole karne, gdzie
próbował ją złapać <b>Krzysztof Smolarek</b>, ale został uprzedzony
przez obrońce, który nieszcześliwie zagrał pod nogi jednego z rywali.
Zawodnikowi Dubidz pozostało tylko wbić piłkę do bramki. W tej sytuacji
błąd popełnił arbiter spotkania nie odgwizdując spalonego. Chociaż było
jeszcze około 15 minut do zakończenia meczu Rolnik nie potrafił odwrócić
losów spotkania. Chociaż okazji ku temu miał co niemiara. Zawiodła
skuteczność.

<p>Mimo dobrej gry i stworzenia sobie wielu świetnych okazji Rolnik
przegrał mecz w Dubidzach. Niestety za wrażenie estetyczne nie dostaje
się punktów. W dzisiejszym spotkaniu cała drużyna zaprezentowała się z
dobrej strony dlatego jest tak bardzo żal tej porażki. Była naprawde
świetna okazja na rewanż za poprzednie spotkanie z Dubidzami. Niestety
brak skutecznoości sprawił że zawodnicy Rolnika nie mogą być zadowoleni
po tym meczu. Z drugiej strony należy cieszyć się z tego jak drużyna
zaprezentowała się na boisku. Jest to dobry prognostyk na następne
spotkania w lidze. Jeżeli uda się jeszcze poprawić skuteczność kolejne
mecze napewno będą wygrane. Jeżeli chodzi o najbliższy mecz to odbędzię
się on w najbliższą sobotę w Skąpej. Rozpoczęcie spotkania jest
zaplanowane na godzinę 16:30. Wszystkich serdecznie na to spotkanie
zapraszamy. Emocji napewno nie zabraknie. <br>
