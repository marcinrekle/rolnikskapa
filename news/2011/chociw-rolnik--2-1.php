<p class="wprowadzenie">Porażki w wyjazdowym spotkaniu z GKS Chociw
niespodziewał się nikt. Niestety po tym spotkaniu to rywale wzbogacili
się o kolejne 3 pkt. Mecz nie był wielkim widowiskiem, wpływ na to miało
bardzo kiepskie boisko a także postawa sędziego który podejmował
niezrozumiałe decyzje. Momentami nie było wiadomo komu się należy piłka
i dlaczego. Jedyną bramkę dla Rolnika zdobył Paweł Tokarski.

<p>Cała pierwsza połowa to przewaga oraz ciągły napór Rolnika. Pod
bramką rywala brakowało dokładności i skuteczności. W dokłądnym
rozgrywaniu piłki przeszkadzała kiepska murawa. Nie wiadomo było jak się
zachowa piłka w momencie kontaktu z boiskiem, dlatego dość często
płatała figle zawodnikom. Pierwsza bramkę niespodziewanie zdobył Chociw.
Po minięciu obrońcy jeden z zawodników gospodarzy zdobył gola w sytuacji
sam na sam. Po tej sytuacji Rolnik ruszył jeszcze mocniej do ataku.
Jednak dosyć długo nie udawało się zdobyć wyrównania. Szkoda bo sytuacje
były. Wyrównującą bramkę zdobył <b>Paweł Tokarski</b>, który przedarł
się w pole karne rywali i uderzeniem w tzw "długi róg" pokonał golkipera
GKS Chociw. Wyrównujący gol nie zadowolił Rolnie jedną bramka, dlatego
ataki nie ustawały. Bramka dająca prowadzenie nie padła.

<p>Początkowe 20 minut drugiej połowy należało do gosodarzy. Później
wszystko wróciło do "normy", czyli to Rolnik atakował. Jednak w
porównaniu do pierwszej części spotkania drużyna ze Skąpej zagrała
gorzej. Zapewne jeśli by się udało zdobyć przynajmniej jedną bramkę
nastroje po tym spotkaniu byłyby zapewne dużo lepsze. A sytuację ku temu
były. W najlepszej z nich trzech graczy Rolnika znalazło się przed
praktycznie pustą bramką, ale żaden nie potrafił tego wykorzystać.
Uderzenia trafiły w bramkarza, a chwilę później sędzia odgwizdał
spalonego. Była to dziwna decyzja ponieważ podanie było od rywala, a jak
wiadomo w takiej sytuacji o spalonym nie może być mowy. Jak mówi
piłkarskie porzekadło "niewykorzystane sytuację się mszczą". Tak było
także w tym meczu. Tuż przed końcem spotkania zwycięską bramkę zdobył
Chociw. Rolnik jeszcze próbował wyrównać ale nie udało się tego dokonać.

<p>Niestety nie udało się przywieść jakichkolwiek punktów z wyjazdu do
Chociwia. Chociaż nikt nie zakładał nawet remisu, a co mówić o porażce.
Bardzo szkoda tych 3 pkt bo dzięki pozostałym wynikom jakie padły w tej
kolejce Rolnik mógł zmniejszyć stratę lidera i wynosiłaby ona 5 punktów. 
A tak nadal tracimy osiem punktów. Tym bardziej szkoda że rywalem 
była przedostatnia drużyna w tabeli. 
<p> Szansę na rehabilitacje Rolnik będzia miał już 3 maja. Chociaż będzie 
o to bardzo trudno, gdyż przyjdzię się nam zmierzyć z LZS Kiełczygłów. 
Spotkanie rozpocznie się o godzinie 17:00. 
