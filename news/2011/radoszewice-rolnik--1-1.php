<p class="wprowadzenie">W kolejnym spotkaniu ligowym Rolnik grał z
kolejną drużyną walczącą o awans. Tym razem przyszło zmierzyć się naszej
drużynie w Radoszewicach z miejscowym KS. Co ostatnio staje się normą
pierwszą bramkę zdobył rywal. Na szczęście za sprawą Marcina Kazuby
udało się zdobyć jeden punkt. Kolejny kandydat do awansu nie potrafi
sprostać Rolnikowi.

<p>Początek spotkania był wyrównany z lekkim wskazaniem na gospodarzy.
Jednak lepszą sytuację na zdobycie prowadzenia miał Rolnik. Po rzucie
wolnym sprzed pola karnego wykonywanym przez <b>Pawła Tokarskiego</b>
piłkę przed samą linią bramkową zatrzymał bramkarz. Akcje gospodarzy
najczęściej kończyły się rzutami rożnymi lub wolnymi. Po jednej z akcji
Radoszewice miały rzut wolnym blisko lini końcowej. Wydawało się że z
tej pozycji można tylko wrzucić piłkę przed bramkę, ale zawodnik
gospodarzy uderzył w kierunku bliższego słupka i piłka po rękach <b>Radka
Kurzynogi</b> wpadła do siatki. Rolnik chciał jak najszybciej odrobić
straty. Po akcji i strzale <b>Dawida Taranka</b> piłkę zmierzającą do
bramki wybił jeden z obrońców. Zawodnicy Rolnika często decydowali się
na strzały, ale większość uderzeń przelatywała obok lub nad bramką(m.in
<b>Paweł Tokarski, Damian Rydz, Tomasz Dygudaj, Marcin Kazuba</b>).
Jeżeli już piłka zmierzała w bramkę to i tak nie sprawiała ona problemów
golkiperowi Radoszewic. Kilka piłek przechodziło wzdłuż bramki i
wystarczyło tylko dołożyć nogę. Tak więc szans na wyrównanie nie
brakowało. Także swoje szansę mieli gospodarze, ale albo piłkę
przejmowali obrońcy albo dobrze bronił bramkarz. Do końa połowy
utrzymało się jednobramkowe prowadzenie Radoszewic.

<p>Od początku drugiej odsłony zawodnicy Rolnika rzucili się do ataku.
Niestety niewiele z tych prób wynikało. Zagrożenie pod bramką było
praktycznie żadne. Wynikało to z tego że drużyna Radoszewic nastawiła
się na grę z kontry, co przy korzystnym wyniku wydawało się rozsądnym
posunięciem. Z każdą upływającą minutą Rolnik atakował coraz większą
ilością zawodników. W końcu udało się zdobyć wyrównującą bramkę. O piłkę
wrzuconą w pole karne z bramkarzem powalczył <b>Kamil Kapela</b>. Z tego
pojedynku zwycięsko wyszedł bramkarz, który zdołał piłke wypiąstkować.
Na jego nieszczeście ta trafiła do <b>Marcina Kazuby</b>, który
wykorzystując to gdzie znajdował się bramkarz, przelobował go. Piłka
wpadła w długi róg pod poprzeczkę. Dobrą sytuację zmarnował <b>Dawid
Taranek</b>, który spudłował znajdując się tylko przed bramkarzem. Obie
ekipy do zakończenia spotkania walczyły o zwycięską bramkę. Żadna tego
nie dokonała. Spotkanie zakończyło się sprawiedliwym remisem 1:1.


<p>W kolejnym spotkaniu Rolnik pokazał że potrafi się pozbierać mimo
niekorzystnego rezultatu, że nie łatwo go złamać. Smuci fakt że znowu
pierwsi tracimy bramkę i musimy gonić rywala. Cieszy że robimy to
skutecznie. Kolejny raz sytuacji bramkowych nie brakowało. Brakowało za
to skuteczności. Ewidentnie widać że lepiej nam się gra przeciwko
silnemu rywalowi który nie nastawia się tylko na obronę, ale także sam
atakuje. Napewno punkt na boisku wicelidera może cieszyć. Chociaż mogły
być o dwa więcej.
<p>
<p>Kolejny mecz Rolnik rozegra na swoim boisku. Do Skąpej przyjedzie
Zryw Ożegów. Spotkanie na chwilę obecną jest zaplanowane na niedzielę 29
maja o 17:00. Wszystkich serdecznie zapraszamy. <br>
