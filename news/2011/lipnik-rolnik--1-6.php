<p class="wprowadzenie">Aż czterech spotkań potrzebował Rolnik by zdobyć
pierwsze punkty w nowym sezonie. Przełamanie nastąpiło w bardzo dobrym
stylu, ponieważ udało się zdobyć aż sześć bramek. Wreszcie drużyna
zagrała skutecznie i wykorzystała większość stworzonych sytuacji. Łupem
bramkowym podzielili się Damian Rydz, Przemysław Mielczarek i Łukasz
Łyp.

<p id="opis1">Pierwsza połowa spotkania nie zapowiadała pogromu
debiutującego w tym sezonie w B klasie Zagłębia Lipnik. Rolnik
przeważał, ale gospodarze stawiali dzielnie opór i kilkakrotnie
zagrozili bramce Rolnika. Już na początku spotkania mogli objąć
prowadzenie ale <b>Radek Kurzynoga</b> złapał piłkę tuż przed linią
bramkową. Lipnik miał także kilka rzutów rożnych jednak za każdym razem
któryś z zawodników Rolnika wyjaśniał sytuację. Rolnikowi trochę
przeszkadzał sędzia spotkania, który po prawie każdym prostopadłym
podaniu gwizdał spalonego. W przynajmniej połowie były to błędne
decyzje. W końcu padła dla nas bramka, a do tego po raz pierwszy w
sezonie strzelcem okazał się napastnik. Mocnym strzałem lewą nogą
bramkarza pokonał <b>Damian Rydz</b>. Jescze w pierwszej połowie
mogliśmy zdobyć kolejną bramkę. Rzut wolny pośredni w polu karnym rywali
wykonywał <b>Łukasz Łyp</b>. Strzał gracza Rolnika przeleciał jednak nad
poprzeczką.

<p id="opis2">Po jednobramkowym prowadzeniu i w miarę wyrównanej
pierwszej połowie nikt się nie spodziewał że w kolejnej padnie tyle
bramek dla Rolnika. Najpierw dwie bramki zdobył <b>Przemek Mielczarek</b>.
Kolejna dołożył <b>Łukasz Łyp</b>. Wtedy nastąpiła dziwna sytuacja.
Jednego z rywali sfaulował <b>Tomasz Osiej</b>, a sędzia podyktował
jedenastke. Co w tym dziwnego? To że faul był pół metra przed polem
karnym. Ale mimo protestów zawodników Rolnika, sędzia swojej decyzji już
nie zmienił. Gracz Lipnika pewnie wykorzystał ten prezent od sędziego.
Ta sytuacja tylko podrażniła naszą drużynę, która konsekwentnie dążyła
do zdobycia kolejnych bramek. A padły jeszcze dwie. Strzelcami byli <b>Damian
Rydz</b> i <b>Łukasz Łyp</b>.

<p id="podsumowanie">Mecz z Lipnikiem pokazał że jednak zawodnicy
Rolnika potrafią wykorzystywać stworzone sytuacje bramkowe. I do tego
wreszcie przebudzili się napastnicy. To cieszy. Cieszy także pierwsze zwycięstwo 
i że kolejny raz nie zwiększa się dystans do lidera. Chociaż wynosi on już 9 punktów. 
Oby za tydzień po kolejnym spotkaniu znów dopisywały nam humory.

<p id="nastepny">Kolejnym przeciwnikiem będzie Park Starzenice. Spotkanie odbędzie 
się w niedziele w Skąpej. W związku z tym, że dwóch naszych kolegów w ten weekend 
zmienia swój stan cywilny, spotkanie zostanie rozegrane już o 12:30. Wszystkich chetnych 
zapraszamy na to spotkanie. Przypominam o możliwości typowanie wyniki tego oraz pozostałych 
spotkań, nagroda dla najlepszego typera jest aparat cyfrowy.  
