<p class="wprowadzenie">Spotkanie derbowe ze Słowianem Dworszowice
niestety zakończyło się porażką 0:2. Na mecz nie dotarł sędzia
wyznaczony przez OZPN, dlatego spotkanie rozoczęło się pół godziny
później a zawody poprowadził trener Słowiana. Obie bramki bramki padły w
pierwszej połowie po błędach obrońcy Rolnika. Mimo usilnych starań i
walki do ostatniego gwizdka nie udało się odwrócić losów spotkania.

<p>Od samego początku spotkanie było wyrównane. Żadna z drużyn nie
potrafiła osiągnąć przewagi, akcje przenosiły się z pod jednego pola
karnego pod drugie. Niestety po prostych błędach w obronie Rolnik
stracił dwie bramki. W obu sytuacjach gole padły po strzałach z bliskiej
odległości, więc <b>Radosław Kurzynoga</b> nie miał najmniejszych szans
na obronę. Rolnik także miał szansę na pokonanie bramkarza Dworszowic. Z
rzutu wolnego obok słupka uderzył <b>Tomasz Osiej</b>. Po strzale z
dalszej odległości <b>Sebastiana Mielczarka</b> piłka przeleciała tuż
nad poprzeczką. Pierwsza połowa zakończyła się lekkim strzałem pod
poprzeczke z woleja <b>Marcina Kazuby</b>. Jednak to uderzenie nie
sprawiło bramkarzowi Słowiana najmniejszego problemu.

<p>Na drugą połowę Rolnik wyszedł pełen animuszu i wiary w możliwość 
zmiany niekorzystnego rezultatu. Od samego początku zarysowała się wyraźna 
przewaga zespołu Skąpy. Drużyna z Dworszowic nastawiła się w drugiej połowie na 
grę z kontry. Jednak stuprocentowych okazji brakowało. Goście ustawili się 
przed swoim polem karnym i nie patyczkując się wybijali piłke, aby tylko 
nie stracić bramki. Pasuje tutaj modne ostatnio powiedzenie o ustawieniu 
autobusu w bramce. W takiej sytuacji trudno o jakieś dobre sytuacje bramkowe. 
W jednej z nich w polu karnym znalazł się <b>Damian Rydz</b>, ale uderzenie 
wylądowało koło bramki. Z rzutów wolnych uderzali <b>Sebastian Mielczarek</b> 
i <b>Paweł Tokarski</b>, niestety bramek z tego nie było. W dobrej sytuacji 
pod bramką Słowiana znalazł się <b>Sebastian Mielczarek</b>, ale zamiast 
zagrywać wzdłuż bramki zdecydował się na strzał. Uderzenie z takiej pozycji raczej nie 
miało sensu, a zagraną piłkę przed bramkę mógłby wepchnąc któryś z graczy 
Rolnika badź Słowiana. W końcówce spotkania największe zagrożenie Rolnik 
stwarzał po wrzutach z autu. Jednak w polu karnym był taki tłok ze trudno było 
uderzyć na bramkę. Drużyna Słowiana wyprowadziła kilka kontr, na szczęście 
świetnie spisywała się obrona lub bramkarz. Wynik spotkania nie uległ zmianie. 
 

<p>Mimo walki do ostatniego gwizdka Rolnik nie zdołał odrobić strat 
poniesionych w pierwszej części meczu. Niestety proste błędy w obronie 
zaważyły na wyniku derbów. Należy drużynę pochwalić za walkę do końca i 
wiarę w możliwość odniesienia korzystnego rezultatu. O tym spotkaniu 
należy jak najszybciej zapomnieć bo już w środę będzie okazja 
do poprawienia sobie nastrojów. Rolnik rozegra zaległe spotkanie z LZS Kiełczygłów. 
Spotkanie odbędzie się w Skąpej o godzinie 18:00. Napewno nie będzie należeć 
do najłatwiejszych.    

