<p class="wprowadzenie">Pierwsze wiosenne spotkanie na swoim boisku
zakończyło się zwycięstwem Rolnika. Po bramkach <b>Dawida Taranka</b>, <b>Łukasza
Łypa</b> i <b>Damiana Rydza</b> okazał się lepszy od Protektora
Sędziejowice. Goście byli w stanie tylko raz pokonać <b>Krzysztofa
Smolarka</b>. Wynik mógł być wyższy ale brakowało szczęścia i przede
wszystkim skuteczności.


<p>Rolnik od samego początku spotkania ruszył do ataku. Zawodnicy
Rolnika potrafili stworzyć sobie kilka sytuacji bramkowych. Jedna z nich
skończyła się bramką. Piłkę w pole karne wrzucił <b>Damian Lesiak</b> a
bramkę strzałem głową zdobył <b>Dawid Taranek</b>. Jest to już jego
druga bramka na wiosne w trzech meczach. Pierwszą zdobył w Buczku, a w
spotkaniu w Dubidzach wywalczył rzut karny. Kolejne szanse bramkowe miał
<b>Paweł Tokarski</b>, który najpierw minimalnie spudłował z rzutu
wolnego . Następnie zdecydował się na strzał sprzed pola karnego, ale
piłka zatrzymałą się na poprzeczce. Kilkanaście centymetrów niżej i
byłaby bramka. Mimo przewagi Rolnika goście doprowadzili do wyrónania. W
zamieszaniu pod naszą bramką na strzał z pierwszej piłki zdecydował się
jeden z graczy Protektora. Piłka uderzona w długi róg wpadłą przy samym
słupku. Strzał był na tyle precyzyjny, że wyciągnięty jak struna <b>Krzysztof
Smolarek</b> piłki nie sięgnął. Rolnik miał szansę na ponowne objęcie
prowadzenia. Niestety strzał głową <b>Łukasza Łypa</b> trafił w słupek.
Sędzia odgwizdał spalonego, przy dobitce. Pierwsza połowa zakończyła się
wynikiem 1:1.


<p>W drugiej połowie Rolnik stworzył sobie jeszcze więcej okazji
bramkowych niż w pierwszej odsłonie spotkania. Jednak dopiero w końcówce
udało się zapewnić zwycięstwo nad Sędziejowicami. W dobrej sytuacji po
prostopadłym podaniu od <b>Marcina Kazuby</b> znalazł się <b>Łukasz Łyp</b>.
Niestety obronną ręką wyszedł bramkarz Protektora. Po rzucie rożnym do
piłki przed bramką doszedł <b>Marcin Kazuba</b>, jednak strzał z woleja
był niecelny. Tak samo uderzenie z lini pola karnego z pierwszej piłki z
lini pola karnego przeleciało nad bramką. W kolejnej sytuacji
wybiegającego daleko przed pole karne do piłki zagranej za obronę
bramkarza Protektora przerzucił <b>Damian Rydz</b>, ale piłki z 15
metrów do pustej bramki nie potrafił skierować <b>Łukasz Łyp</b>.
Zawodnik Rolnika przerzucił piłkę nad bramką a mógł z nią zrobić
wszystko. Jednak niedługo później zrehabilitował się za tą sytuacje. Po
wrzucie z autu <b>Tomasz Dygudaja</b>, głową zdobył prowadzenie dla
Rolnika. W ciągu kolejnych minut rywal został "dobity". Trzecią bramkę
dla Rolnika zdobył <b>Damian Rydz</b>. Przejął piłke w polu karnym i
strzelił nie do obrony. Chwilę później padła czwarta bramka dla Skąpej.
Niestety ku zdziwieniu wszystkich nie została ona uznana. Po rzucie
rożnym <b>Seweryn Włodarczyk</b> uderzył głową a piłka pół metra
przekroczyła linię bramkową. Po spotkaniu arbiter tłumaczył się że piłka
była zasłonięta i nie widział że przekroczyła linię. Pod koniec puściły
nerwy piłkarzą gości. Po bardzo ostrym wejściu w nogi <b>Tomasza Osieja</b>
jeden z zawodników rywali został wyrzucony z boiska. Chwilę później
sędzia zakończył zawody.

<p>Dzięki dobrej końcówce Rolnik zainkasował komplet punktów w meczu z
Protektorem Sędziejowice. Drużyna do samego końca wierzyła w zwycięstwo
i to przyniosło zamierzony efekt. To cieszy, a także fakt trzykrotnego
pokonania golkipera rywali. Troche żal niewykorzystanych sytuacji. Z
pierwszej szóstki naszej grupy porażki doznały tylko Dubidze, więc tylko
do nich zostały odrobione punkty. Do lidera strata nadal wynosi 8
punktów, natomiast do miejsca drugiego premiowanego spotkanie barażowym
brakuje 6 oczek. Następne spotkanie Rolnik rozegra za 2 tygodnie w
sobotę 30 kwietnia. Jest to spowodowane przerwą z okazji Świąt
Wielkanocnych. Zagramy w Chociwiu z miejscowym LZS. <br>
