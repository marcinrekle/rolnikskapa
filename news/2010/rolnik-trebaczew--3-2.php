     <p class="wprowadzenie">Po dwóch kolejnych porażkach Rolnik w końcu się przełamał i odniósł zwycięstwo nad LZS Trębaczew. Bramki zdobyli Damian Rydz, Kamil Kapela i Paweł Tokarski. Mimo tylko jednobramowego zwycięstwa Rolnik był dużo lepszą drużyną.<br>
     <p>Ostatnie spotkanie w tym roku na swoim boisku Rolnik rozpoczął ze zmianami w składzie i pozycji zawodników. Do pomocy zostali przesunięci <b>Kamil Kapela</b> i <b>Marcin Kazuba</b>, do ataku <b>Paweł Tokarski</b>. Parę środkowych obrońców tworzyli <b>Andrzej Chachurski</b> i <b>Jerzy Trzepański</b>. 
	  <p>Rolnik zaatakował od początku spotkania i osiągnął przewagę. Aktywna była zwłaszcza lewa strona. Jednak nie było za wiele klarownych sytuacji do zdobycia bramki.Brakowało celnego ostatniego podania lub skuteczności pod bramką rywali. Mimo to Rolnik powinien objąć prowadzenie. Po rozegraniu piłki z lewej strony na przedpole bramki dośrodkował <b>Kamil Kapela</b>a próbującego oddać strzał Damiana Rydza faulował bramkarz rywali. Do jedenastki podszedł Kamil Kapela ,niestety strzelił prosto w bramkarza, który bez problemu złapał piłkę. Rywale nie potrafili zagrozić Rolnikowi. Pierwsza połowa zakończyła się bezbramkowym remisem.   
	  <p>Druga część spotkania także rozpoczęła się od ataków Rolnika. Z każdą minutą rosła przewaga naszego zespołu ale to rywale zdobyli prowadzenie. Po niepotrzebnym faulu w okolicach środka pola rywale wykonywali rzut wolny. Piłka została wrzucona w pole karne i wydawało się że padnie łupem bramkarza. Niestety bramkarz wypuścił piłkę z rak i ta wpadła do bramki. Wydawało się że ta sytuacja załamie zawodników, jednak minutę później Rolnik cieszył się z remisu. Sytuację sam na sam z bramkarzem wykorzystał <b>Damian Rydz</b>. Wszyscy liczyli że ta bramka doda pewności siebie zawodnikom i kolejne szybko padną. Ku zaskoczeniu wszystkich drużyna Trębaczewa zdobyła drugiego gola, który można powiedzieć był kopią pierwszego. Po raz kolejny nie popisał się <b>Krzysztof Smolarek,</b> wypuszczając piłkę dośrodkowaną w pole karne. Próbował jeszcze uratować sytuację i wybił piłkę z linii bramkowej. Niestety zabrakło asekuracji i piłkę do bramki dobił zawodnik Trębaczewa. Należy także wspomnieć o czerwonej kartce, która zapewne miał wpływ na końcowy wynik spotkania. Wychodzącego na czystą pozycję Damiana Rydza sfaulował zawodnik Trębaczewa. Sędzia nie miał wątpliwości i od razu sięgnął po czerwony kartonik mimo protestu przyjezdnych.
	  Od tej chwili zawodnicy rolnika mieli więcej miejsca na boisku co skrzętnie wykorzystali. W kolejnym, drugim już spotkaniu, bramkę zdobył Kamil Kapela wykorzystując dośrodkowanie z rzutu wolnego Pawła Tokarskiego. Przy uderzeniu głową bramkarz był bez szans. Jednak remis piłkarzy Rolnika nie zadowalał i atakowali dalej aby zdobyć zwycięską bramkę. Udało to się im około 10 minut przed końcem spotkania. Z ostrego kąta uderzał <b>Paweł Tokarski</b> a piłka jeszcze odbiła się od słupka i wpadła do bramki. Po tej sytuacji Rolnik nastawił się na grę z kontry i obronę korzystnego rezultatu. Do końca spotkania wynik nie uległ zmianie.</b>  	      	      	<br> 
<p>Jak widać przesunięcie zawodników na inne pozycję przyniosła efekt w postaci trzech punktów. Na obecną chwilę wydaję się że ten skład i to ustawienie jest optymalne. Pewność co do tego będzie można stwierdzić po kilku spotkaniach.	  <br><b>Już za tydzień w niedzielę zakończenie rundy jesiennej obecnego sezonu. W meczu w Makowiskach Rolnik spróbuję pokonać liderujący Płomień. Zapraszam na to spotkanie.</b>
