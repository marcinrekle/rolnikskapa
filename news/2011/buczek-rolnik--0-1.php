<p class="wprowadzenie">Twierdza Buczek zdobyta!!! Zawodnicy Rolnika
jako pierwsi w sezonie potrafili zwyciężyć na boisku Orkana II Buczek.
Mimo przewagi gospodarzy to jednak Rolnik potrafił zdobyć tą jedyną
bramkę i zgarnąć komplet punktów. Zwycięstwo na 3 minuty przed końcem
zapewnił <b>Dawid Taranek</b>.


<p>W pierwszej odsłonie spotkania żadna z drużyn nie potrafiła osiągnąć
przewagi. Tak jedna jak i druga drużyna starały się atakować. Jednak
sytuacji podbramkowych było mało. Bardzo dobrze w pierwszej połowie, jak
i w całym meczu, spisywała się obrona Rolnika i bramkarz. Na początku
meczu na strzał z dystansu zdecydował się aktywny tego dnia <b>Paweł
Tokarski</b>, ale to uderzenie nie zagroziło bramkarzowi Orkanu II.
Niedługo później ten sam zawodnik czystym wślizgiem odebrał piłkę, ale
nie wiedzieć czemu sędzia odgwizdał przewinienie i pokazał naszemu
zawodnikowi żółtą kartke. Kolejna szansa była po bardzo dobrym uderzeniu
z woleja <b>Damiana Rydza</b>, niestety piłka minęła bramkę rywali.
Jeżeli chodzi o rywali mieli parę rzutów wolnych sprzed pola karnego,
jednak za każdym razem sytuacje wyjaśniał bramkarz badź obrona. Po
błędzie sędziego, który nie odgwizdał spalonego, bardzo dobrze spisał
się <b>Krzysztof Smolarek</b> broniąc w sytuacji sam na sam. W pierwszej
połowie Rolnik stworzył sobie jeszcze jedną dobrą sytuacje, ale w polu
karnym rywala piłke źle przyjął <b>Tomasz Dygudaj</b>. Pierwsze 45 minut
zakończyło się bezbramkowym remisem.


<p>Druga połowa już nie byla taka wyrównana jak pierwsza. Stroną
przeważającą byli gracze z Buczku. Parokrotnie pod bramką Rolnika było
"gorąco". Na szczęscie albo bardzo dobrze bronił bramkarz albo świetnie
spisywała się obrona. Rolnik stwarzał zagrożenie pod bramką rywala po
kontrach. Po jednej z nich w idealnej sytuacji przed bramką znalazł się
<b>Tomasz Krawczyk</b>, ale strzał oddał prosto w bramkarza. Była to
doskonała sytuacja do objęcia prowadzenia. 15 minut przed końcem
spotkania na strzał sprzed pola karnego zdecydował się <b>Marcin Kazuba</b>.
Strzał był celny, ale zbyt słaby aby zaskoczyć golkipera Orkanu. Kiedy
wydawało się że spotkanie zakończy się bezbramkowym remisem, ten sam
zawodnik przejął piłkę i odegrał do <b>Seweryna Włodarczyka</b>.
Pomocnik Rolnika popędził lewą stroną boiska i dograł piłkę w pole
karne. Do tej doszedł <b>Dawid Taranek</b>i z najbliższej odległości
umieścił w siatce. W drużynie Rolnika wybuchła niesamowita radość. Było
to w 87 minucie meczu więc trener nakazał praktycznie całą drużyną
bronić wyniku. W ostatniej minucie meczu Orkan wywalczył rzut wolny przy
lini bocznej. Zawodnik z Buczku odepchnął <b>Pawła Tokarskiego</b>, a
sędzia ukarał zółtą kartką naszego zawodnika. Jako że było to już druga
kartka Rolnik spotkanie kończył w dziesiątke. Po rzucie wolnym sędzia
zakończył spotkanie.


<p>Nie jest sztuką wygrać, kiedy wszystko wychodzi. Sztuką jest sięgnąć
po pełną pulę, gdy gra się nie klei.Tak można skomentować mecz naszej
drużyny w Buczku. Rolnik rundę wiosenną rozpoczął od zwycięstwa na
trudnym terenie. Trudnym ponieważ w tym sezonie gospodarze na swoim
boisku jeszcze nie przegrali. W tym meczu bardzo dobrze spisywała się
formacja defensywna oraz bramkarz. Trochę niżej należy ocenić pomocników
oraz napastników. Ale najważniejsze są kolejne 3 punkty. Wyniki
pozostałych spotkań sprawiły że Rolnik odrobił punkty do trzech drużyn.
Strata do dwóch pierwszych drużyn wynosi w tej chwili 5 punktów i
napewno jest do odrobienia. W najbliższy weekend Rolnik rozegra kolejny
mecz wyjazdowy, tym razem zmierzy się z Dubidzami. <br>
