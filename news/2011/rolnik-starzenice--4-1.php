<p class="wprowadzenie">Rolnik wygrywa drugie spotkanie w rundzie
jesiennej. Spotkanie rozpoczęło się z opóźnieniem z powodu problemów z
autokarem zespołu gości. Mimo wielu świetnych sytuacji Rolnik zdobył
tylko cztery bramki. Znowu dała o sobie znać największa bolączka naszej
drużyny, czyli nieskuteczność. Bramki zdobyli Paweł Tokarski, Marcin
Kazuba, Damian Rydz oraz Przemysław Mielczarek.

<p id="opis1">W pierwszej połowie lepszą drużyną był Rolnik. Nasza
drużyna stworzyła sobie wiele dobry i bardzo dobrych sytuacji
bramkowych. Z tego wykorzystał dwie. Pierwsza była dziełem <b>Pawła
Tokarskie</b>, który dostał podanie na skrzydło ze środka boiska. W
sytuacji sam na sam silnym strzałem pokonał golkipera Starzenic. W
bardzo dobrej sytuacji ten sam zawodnik stojąc tuż przed bramką uderzył
nad poprzeczką. Druga bramka padła po rzucie karnym,który wykonywał <b>Marcin
Kazuba</b>. Jedenastka została podyktowany za faul na <b>Przemysławie
Mielczarku</b>. Z decyzją sędziego nie potrafili pogodzić zawodnicy
Starzenic, jeden z nich za sugestie w kierunku sędziego o sprzedanie
meczu został wyrzucony z boiska. Rolnik prowadził dwoma bramkami i grał
w przewadze , wydawało się że ma rywali 'na widelcu'. Jednak gościom w
pierwszej połowie udało się złapać kontakt. Po wrzucie z autu w pole
karne jeden z zawodników Parku uderzył na bramkę, piłkę nogą próbował
odbić <b>Radosław Kurzynoga</b>. Jednak żle w nią trafił i ta wpadła do
siatki. Goście mogli także zdobyć drugiego gola, ale bardzo dobrze
spisał się nasz bramkarz. W dobrym stylu zatrzymał strzał rywala. Mimo
stworzenia sobie wielu dobrych sytuacji Rolnik schodził na przerwę z
jednobramkowym prowadzeniem.

<p id="opis2">Po przerwie zawodnicy Rolnika zaprezentowali indolencje
strzelecką. W poprzednich spotkaniach marnowali wiele sytuacji ale tym
razem przeszli samych siebie. Nawet w najlepszych sytuacjach pudlowali
niemiłosiernie. To mogło się zemścić. Co prawda zawodnik gości umieścił
piłkę w siatce ale znajdował się na minimalnym spalonym. Jeżeli sędzia
by to trafienie uznał byłby remis. Dopiero w ostatnim kwadransie Rolnik
zdołał zdobyć bramki i uniknąć nerwowej końcówki. Bramki strzelili <b>Damian
Rydz</b> i <b>Przemysław Mielczarek</b>. Pierwsza to sytuacja sam na
sam, a druga strzał do pustej bramki.


<p id="podsumowanie">Po jednym spotkaniu powrócił koszmar Rolnika. Tylu
zmarnowanych sytuacji w tym sezonie jeszcze nie było. Dobrze że rywal
nie był wymagający, a do tego większość spotkania grał w osłabieniu.
Silniejszy rywal do takiej ilości sytuacji napewno nie dopuści. Trzeba
wykorzystywać to co się stworzy, bo przypadkowa bramka rywala może
pozbawić Rolnika punktów.


<p id="nastepny">Już za tydzień czeka nas pojedynek derbowy ze Słowianem 
Dworszowice. Napewno nie będzie to łatwa przeprawa, ponieważ na to spotkania 
obie drużyny są bardzo zmotywowane,żeby pokonać lokalnego rywala. Napewno 
zapowiada się ciekawe spotkanie. Zostanie ono rozegrane w Skąpej o 15:30. 
Wszystkich serdecznie na to spotkanie zapraszamy.

