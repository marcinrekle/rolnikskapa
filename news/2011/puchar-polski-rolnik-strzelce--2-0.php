<p class="wprowadzenie">W pierwszej rundzie Pucharu Polski okręgu
sieradzkiego doszło do spotkania derbowego pomiędzy drużynami z gminy
Strzelce Wielkie. Na boisku w Skąpej stanęły naprzeciwko siebie drużyna
miejscowego Rolnika oraz GLKS Strzelce Wielkie. Spotkanie zakończyło się
zwycięstwem B klasowego Rolnika, który dwukrotnie potrafił znaleźć drogę
do bramki rywala. Natomiast występująca od tego sezonu w klasie
okręgowej drużyna Strzelec nie potrafiła ani razu pokonać bramkarza
gospodarzy. Obie bramki w tym spotkaniu zdobył Dawid Taranek.
<p>Od początku meczu wyżej notowany rywal starał się zdominować Rolnika.
Niby drużyna Strzelec dłużej utrzymywałą się przy futbolówce jednak nic
z tego nie wynikało. Rywal wymieniał duża ilość podań na swojej połowie,
jednak im było bliżej bramki rywali tym wyglądało to znacznie gorzej.
Strzelce nie potrafiły stworzyć sobie dogodnej sytuacji. Duża w tym
zasługa obrony Rolnika i czujnie grającego na przedpolu bramkarza.
Rolnik czychał na kontry oraz starał się zaskoczyć rywali po stałych
fragmentach gry. Prowadzenie mogliśmy objąć po strzale z woleja z
dalszej odległości w wykonaniu <b>Przemysława Mielczarka</b>. Jednak
bardzo dobrze zachował się w tej sytuacji bramkarz gości odbijając
piłke. Kolejną dobrą interwencje zanotował przy strzale głową z bliskiej
odległości <b>Jerzego Trzepańskiego</b>. W między czasie w sytuacji sam
na sam znalazł się zawodnik gości. Na szczęście swiętną interwencja <b>Radosław
Kurzynoga</b>zażegnał niebezpieczeństwo. Pierwsza połowa zakończyła się
wynikiem bezbramkowym.


<p>W drugiej połowie spotkanie stało się bardziej wyrównane, chociaż z
lekkim wskazaniem na drużynę gości. Rolnik wyszedł wyżej i starał się
atakować rywali na ich połowie. Efektem tego były coraz lepsze sytuacje
pod bramką Strzelec. Pierwsza okazja to strzał zza pola karnego <b>Kamila
Kapeli</b> sparowany na poprzeczke przez golkipera gości. W następnej
sytuacji po akcji lewą strona do piłki przed polem karnym dopadł <b>Damian
Rydz</b>, ale nie dał rady wykorzystać tej dobrej sytuacji. Kolejna
akcja Rolnika zakończyła się bramką. Piłkę w polu karnym przyjął <b>Damian
Rydz</b>, odegrał do boku do <b>Marcina Kazuby</b>, a ten dośrodkował w
pole karne. Piłke do bramki głową skierował <b>Dawid Taranek</b>.
Drużyna Strzelec starała się odrobić stratę, jednak wszystkie ataki
rozbijała obrona Rolnika. Po jednej z kontr Rolnika w świetnej sytuacji
przed bramką znalazł się <b>Dawid Taranek</b>. Wszyscy byli pewni
kolejnej bramki dla Rolnika, jednak piłka minęła bramkę. Niedługo
później ten sam zawodnik zrehabilitował się za niewykorzystaną sytuację.
Uderzył z woleja w samo okienko piłkę zgraną głową przez <b>Damiana
Rydza</b>. Przed końcem spotkania z boiska został wyrzucony jeden z
graczy gości za drugą zółtą kartkę. W samej końcówce mnożyły się faule
piłkarzy Srzelec, nie potrafiących pogodzić się z porażką z niżej
notowaną drużyną. Jeżeli chodzi o sytuacje dla Strzelec tylko w jednej
do wysiłku został zmuszony bramkarz Rolnika.

<p>Bardzo udanie rozpoczął się dla Rolnika nowy sezon. Zwycięstwo w
prestiżowym meczu derbowym ze Strzelcami daje nadzieje na sukces w B
klasie. Chociaż po jednym udanym spotkaniu nie wolno popadać w
hurraoptymizm. Jeszcze wiele pracy przed drużyną. Co do samego meczu
cieszy zaangażowanie oraz determinacja wszystkich zawodników którzy
mieli możliwość zaprezentowania się na tle zespołu z "okręgówki". Nie
widać było jakiś dużych dysproporcji w umiejętnościach obu drużyn.
Jeżeli w meczach ligowych będzie podobna frekwencja oraz zaangażowanie
marzenia o awansie mogą się spełnić. <br>
