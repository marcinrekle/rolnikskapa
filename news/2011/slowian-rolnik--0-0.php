<p class="wprowadzenie">Spotkanie derbowe zakonczyło się sprawiedliwym
remisem. W pierwszej połowie drużyną przeważającą był Słowian, natomiast
w drugiej inicjatywe posiadał Rolnik. Obie drużyny stworzyły sobie dobre
sytuacje, ale nie potrafiły ich wykorzystać. Kiepsko spisywał się
arbiter który podejmował decyzje na korzyść gospodarzy. Ale to nie
ekstraklasa i "drukarz" nie zostanie ukarany.

<p id="opis1">Od początku spotkania inicjatywę przejęły Dworszowice. Gra
toczyła się głównie na połowie Rolnika. Nasi zawodnicy nie potrafili
przytrzymać piłki w przodzie i ta szybko wracała przed bramkę Rolnika.
Na szczęście żadna z sytuacji Słowiana nie zakończyła się bramką.
Strzały były niecelne lub bronił bramkarz. Od samego poczatku rywalom
pomagał sędzia. W 20 min podyktował rzut wolny pośredni sprzed pola
karnego oraz ukarał zółtą kartką zawodnika Rolnika. To była kara za
głośne domaganie się sprawiedliwości przez faulowanego gracza Rolnika. W
30 minucie arbiter przeszedł sam siebie wyrzucając z boiska bramkarza
Rolnika. Najpierw pokazał żółta kartkę za dyskusje (kapitan ma do tego
prawo), a później czerwoną za komentarz tej sytuacji. To bardzo
skomplikowało sytuację Rolnika. Wcześniej Rolnik mógł objąć prowadzenie.
Po rzucie rożnym <b>Przemysława Mielczarka</b> najwyżej w polu karnym
wyskoczył <b>Paweł Tokarski</b>, ale jego uderzenie minęło bramkę
gospodarzy. Już grając w osłabieniu także mogłą paść bramka. Jednak do
prostopadłej piłki nie doszedł <b>Damian Rydz</b>. Został uprzedzony
przez bramkarza rywali. Kilka sytuacji stworzyły sobie także Dworszowice
. Jeden ze strzałów trafił w poprzeczkę, drugi w pięknym stylu obronił <b>Tomasz
Dygudaj</b>. Pozostałe mijały bramkę Rolnika.

<p id="opis2">W drugiej części spotkania mimo grania w osłabieniu to
Rolnik był stroną przeważającą. I miał dużą szansę przechylic szale
zwycięstwa na swoją stronę. Zawiodła skuteczność. Zagraną przed bramkę
piłkę odbił bramkarz Słowiana, do piłki doszedł <b>Marcin Kazuba</b>,
jednak jego uderzenie zostało zablokowane. W sytuacji sam na sam piłkę
obok bramki skierował <b>Przemysław Mielczarek</b>. To była końcówka i
możliwe że rywale po tym ciosie by się już nie podnieśli. Pare razy
niecelnie głową uderzał <b>Łukasz Łyp</b>. Pod koniec spotkania sytuację
mieli <b>Damian Rydz</b> i <b>Przemek Mielczarek</b>, ale chyba obaj nie
wiedzieli kto ma tą akcje wykończyć. Gospodarze w tej części nie mieli
dobrych sytuacji. Najlepsza to silny strzał z około 25 metrów. Jednak
piłka leciała w sam środek bramki i bez problemu została złapana przez
golkipera Rolnika.



<p id="podsumowanie">Należy cieszyć się ze zdobytego punktu w derbach,
zwłaszcza że został osiągnięty przy przewadze liczebnej rywali. Chociaż
pozostaje lekki niedosyt, ponieważ komplet punktów był na wyciągnięcie
reki. Brawo dla drużyny za walkę.


<p id="nastepny">W najbliższej kolejce Rolnik pauzuje. Kolejne spotkanie
w B klasie rozegramy więc za dwa tygodnie. Do Skąpej przyjedzie lider,
czyli Zryw Ożegów. To spotkanie trzeba wygrać aby mysleć o wysokim
miejscu na koniec rundy jesiennej. Spotkanie rozpocznie się o godzinie
14:30. Wszystkich serdecznie zapraszamy. <br>
