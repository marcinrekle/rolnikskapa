     <p class="wprowadzenie">W porównaniu do poprzedniego sezonu tym razem w meczu derbowym pomiędzy Słowianem a Rolnikiem nie padły bramki. Co może dziwić gdyż obie drużyny stworzyły sobie wiele sytuacji strzeleckich. Zawodnikom niekiedy brakowało zimnej krwi innym razem brakowało szczęścia.<br>
     <p>Początek spotkania był nerwowy w wykonaniu graczy Rolnika, mnożyły się niecelne zagrania, nieudane wybicia piłki. Dopiero po paru minutach zawodnicy opanowali stres i zaczęli stwarzać sobie sytuacje bramkowe. Najpierw w sytuacji sam na sam z Markiem Zielińskim znalazł się <b>Michał Kapela </b>, niestety nie potrafił wykorzystać tej szansy. Druga okazja trafiła się po błędzie środkowego obrońcy Słowiana, który przepuścił długie podanie od obrony. Do piłki dopadł <b>Damian Rydz</b>,który popędził w kierunku bramki i stanął oko w oko z golkiperem gospodarzy. Mimo że nie był przez nikogo atakowany oddał bardzo lekki strzał prosto w ręce Marka Zielińskiego. Niewykorzystane sytuacje mogły się zemścić na Rolniku. W bardzo dobrej sytuacji po rzucie wolnym sprzed pola karnego przez błąd w kryciu znalazł się gracz Słowiana. Przez nikogo nie atakowany mógł zdobyć prowadzenie ale w sobie tylko wiadomy sposób zmarnował wyborną szanse. W pierwszej połowie było jeszcze kilka sytuacji na zmianę wyniku, ale nie były one aż tak klarowne jak te wyżej opisane.
	  <p>W drugiej połowie obu drużynom do zwycięstwa zabrakło po prostu szczęścia. Piłka kilkakrotnie odbijała się od poprzeczki oraz jeden raz od słupka. Jako ciekawostkę można dodać, że w jednej z akcji piłka po strzałach zawodników Słowiana dwukrotnie lądowała na poprzeczce. Natomiast jeden z graczy Rolnika trafił w słupek, oczywiście piłką ;). W kilku innych sytuacjach zawodnicy nie popisali się skutecznością. Albo trafiali prosto w bramkarza rywala, bądź niemiłosiernie pudłowali. Często dochodziło do zamieszania tak i w jednym jak i w drugim polu karnym, jednak za każdym razem obrońca udawało się wybić piłkę.
	  <p>Podsumowując to spotkanie można śmiało stwierdzić że obie drużyny mogły wysoko pokonać lokalnego rywala. Jednak brak skuteczności i szczęścia zadecydował o tym że kibice nie obejrzeli żadnej bramki, a obie drużyny podzieliły się punktami.	      	<br> 
	  <br><b>Już za tydzień Rolnik będzie miał kolejna szansę na powiększenie dorobku punktowego. Do Skąpej zawita wicelider, drużyna KS Radoszewice. Naszą drużynę czeka nie mniej trudny mecz niż ten ze Słowianem.</b>
