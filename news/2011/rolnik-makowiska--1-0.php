<p class="wprowadzenie">Sezon 2010/11 Rolnik kończył spotkaniem na swoim
boisku z Płomieniem Makowiska. W związku z zwerfykowanie dwóch spotkań z
udziałem Orkanu II Buczek jako walkowerów przed goścmi niespodziewanie
pojawiła się szansa na udział w barażach. Potrzebne do tego były
korzystne wyniki innych spotkań plus zwycięstwo. Żaden warunek nie
został spełniony. W przypadku Rolnika istniała możliwość awansu o jedną
pozycję wyżej. Należało pokonać Makowiska i liczyć na porażkę
Dworszowic. I tak się właśnie stało. Rolnik po bramce Damiana Rydza
odniósł zwycięstwo, a Słowian uległ 1:2. Nasza drużyna rozgrywki
zakończyła na 8 pozycji.

<p>Spotkanie rozpoczęło się z 10 minutowym opóźnieniem, a było to
spowodowane problemami gości ze skompletowanie jedenastki. W tym meczu
nie brakowało ciekawych akcji, walki, przepychanek na boisku. Najgorszym
aktorem tego widowiska okazał się arbiter, który często podejmował
dziwne, mylne decyzje. Jego postawa wprowadzała na boisku nerwowość, co
skutkowało częstymi przepychankami. Za niegroźny faul potrafił dać żółtą
kartkę, i taką samą wyciągał w przypadku ataku na rywala bez piłki.
Jeżeli chodzi o sytuacje na boisku to od początku lekką przewagę
osiągnął Rolnik. Jednak w akcjach brakowało precyzji. W jednej z
sytuacji <b>Michała Kapelę</b> w polu karnym w nieprzepisowy sposób
powstrzymywali obrońcy. Gwiżdżący na korzyść gości sędzia przewinienia
nie widział. Po dośrodkowaniu z lewej strony <b>Pawła Tokarskiego</b> w
znakomitej sytuacji znalazł się <b>Damian Rydz</b>. Niestety uderzenie
głową miniamlnie minęło bramkę gości. Ale co się odwlecze to nie
uciecze. W podobnej sytuacji, tym razem po dośrodkowaniu Adama Pazery,
już się Damian nie pomylił. Tuż przed końcem pierwszej połowy goście
zmarnowali świetną okazje na doprowadzenie do remisu. Na szczęście próba
lobowania nie powiodła się. <br>

Druga część spotkania była podobna do pierwszej. Nie brakowało spięc za
to akcji bramkowych było jak na lekarstwo. Piłkarze Rolnika raczej byli
bliżej podwyższenia prowadzenia niż Makowiska odrobienia straty.
Najlepszą sytuację zmarnował <b>Jerzy Trzepański</b>. Znajdując się tuż
przed bramką przeniósł piłkę nad poprzeczką. Druga szansa to strzał z 20
metrów <b>Marcina Kazuby</b>, jednak piłka przeleciała tuż nad
poprzeczką. Do samego końca spotkania zawodnicy Rolnika starali się
utrzymywać piłkę na połowie rywali. Dzięki temu udało się utrzymać
korzystny wynik do samego końca. <br>


<p>Dobrze Rolnik zakończył średnio udany sezon B klasy. Pokonując
Makowiska udało się wyprzedzić lokalnego rywala. Drużyna pokazała że
jeżeli chce to potrafi dobrze zagrać i takie wpadki jak przed tygodniem
to zwykły przypadek przy pracy. Jeżeli chodzi o ten sezon to już koniec.
Najważniejszy jest teraz kolejny. Trzeba mieć nadzieje że zawodnicy będą
potrafili się zmobilizować i takie sytuacje jak w tym sezonie nie będą
miały miejsca. Tą drużynę stać na wiele co pokazały mecze z drużynami z
czołówki. Potrzebna jest stabilizacja i obecność zawodników na meczach
oraz chociaż raz w tygodniu na treningu.


