<p class="wprowadzenie">W drugim kolejnym spotkaniu w bardzo łatwy
sposób Rolnik traci na początku spotkania bramki. Nie minęło 20 minut a
LKS Kiełczygłów prowadził już 3:0. Jednak w tym spotkaniu Rolnik
wreszcie pokazał charakter i już do przerwy odrobił dwie bramki. Po
przerwie doprowadził do remisu i do samego końca walczył o pełną pulę.

<p>Od samego początku duża przewagę osiągneli goście. Zawodnicy Rolnika
mieli problem z wyjściem ze swojej połowy. Pokłosiem tego było że już w
drugiej minucie Kiełczygłów objął prowadzenie. Na strzał sprzed pola
karnego zdecydował się jeden z gości i piłka wpadła przy słupku obok
bezradnego <b>Radka Kurzynogi</b>. Niedługo potem było 0:2, niestety tym
razem po błędzie naszego bramkarza, który zamiast łapać piłkę skiksował
przy próbie wykopnięcia jej. Na trzecią bramkę nie trzeba było czekać
długo. Rolnik nie potrafił wybić piłki z pola karnego. Dopadł do niej
jeden z zawodników gości i umieścił w siatce. W pierwszym kwadransie
Rolnik praktycznie nie istniał na boisku. Istniała obawa że odbędzie się
'rzeź niewiniątek'. Przełomowym momentem okazała się sytuacja sam na sam
w której świetnie wybronił nasz bramkarz. Zawodnicy Rolnika zaczęli
wreszcie co raz częściej gościć pod bramką Kiełczygłowa, stwarzać sobie
sytuacje, oddawać strzały. W końcu dopięli swego. Strzał <b>Adama Pazery</b>odbił
golkiper gości. Piłka znalazła się pod nogami <b>Michała Kapeli</b>, ale
jakimś cudem piłkę ręką odbił bramkarz. Na szczęście tam gdzie trzeba
znalazł się <b>Damian Rydz</b>i zdobył bramkę. Kontaktowa bramka padła
po bardzo ładnej kontrze. Piłkę przed naszym polem karnym odebrał <b>Marcin
Kazuba</b>, zagrał do <b>Pawła Tokarskiego</b>, następnie piłka trafiła
do <b>Kamila Kapeli</b>. Zawodnik Rolnika podciągnał z piłka kilka
metrów i świetnie zagrał na wolne pole do <b>Dawida Taranka</b>, który
zagrał do niepilnowanego w polu karnym <b>Damiana Rydza</b>. Nasz
napastnik pewnie pokonał bramkarza Kiełczygłowa. Było to naprawdę
widowiskowa akcja, rozegrana w bardzo dobrym tempie, piłka między
zawodnikami wędrowała jak po sznurku. Jeszcze przed przerwą zawodnicy
mieli dwie dobre szanse do doprowadzenie do remisu. Jednak wynik się nie
zmienił.

<p>Po przerwie drużyna Rolnika ruszyła do zdecydowanych ataków na bramkę
gości. Niestety przez dłuższy czas wyrównująca bramka nie padała. Gra
wyglądała dobrze, zawodnicy grali zespołowo i często przedostawali się
pod bramkę rywali. Jednak sytuacji bramkowych nie było za wiele.
Najczęściej po rzutach wolnych. Kiełczygłów często zatrzymywał akcje
Rolnika w sposób neprzepisowy. Wyrównującą bramkę zdobył <b>Paweł
Tokarski</b>, który przejął piłkę w polu karnym i uderzył bez
zastanowienia. W drużynie Rolnika zapanowała wielka radość. Obydwu
drużyn remis nie zadowalał. Na boisku nie brakowała walki i akcji z obu
stron. Momentami wkradała się nerwowość przez ostre zagrania. Inicjatywę
ciągle posiadał Rolnik. Co zaowocowało dwoma dobrymi sytuacjami. W
pierwszej po zagraniu głową <b>Damiana Rydza</b> w sytuacji sam na sam
znalazł się <b>Dawid Taranek</b>. Jednak jego strzał minął w bliskiej
odległości dalszy słupek bramki gości. Drugą szansę miał <b>Tomasz
Dygudaj</b>, ale nie zdołał on dojść do piłki i ta opuściła boisko.
Kiełczygłów swoje sytuacje miał po strzałach z rzutów wolnych. Jednak
wszystkie mijały bramkę. Spotkanie zakończyło się wynikiem remisowym.

<p>Jak na rozgrywki B klasowe był to napewno bardzo dobry mecz, który
się oglądało z przyjemnościa. Nie brakowało w nim walki, strzałów, parad
bramkarskich, ładnych akcji a przede wszystkim bramek. Padło ich aż
sześć. Obie drużyny drużyny grały ofensywnie więc spotkanie oglądało się
przyjemnie. Walka o korzystny wynik trwała do ostatniego gwizdka
sędziego. Sprawiało to że w pewnych momentach zawodnikom puszczały
nerwy.
<p>Jeżeli chodzi o postawę Rolnika na minus trzeba zaliczyć kiepski
początek meczu kiedy to dali sobie strzelić trzy bramki. Na kolejne
spotkanie napewno trzeba wyjść bardziej skoncentrowanym. Drugi minus to
duża ilość niepotrzebnych fauli i niecelnych podań, chociaż tych
ostatnich nie było kto wie jak ile. Te w końcówce można tłumaczyć
zmęczeniem. Teraz o plusach. Napewno cieszy walka i wiara do samego
końca w korzystny rezultat. Rolnik po trzech ciosach na początku meczu
porafił uniknąć czwartego po którym by się już mógł nie podnieść. A
najważniejsze że sam był w stanie jeszcze przed przerwą dwa razy
rywalowi 'oddać'. Po przerwie udało się wyprowadzić jeszcze jeden cios i
zremisować. Chociaż szansa na zwycięstwo też była. Cieszy także ładna
gra. To że zawodnicy grali piłka i po kilku podaniach potrafili znaleźć
się pod bramką rywali. Kolejne dobre spotkanie rozegrała obrona do
której można mieć chyba najmniej zastrzeżeń w przekroju całej rundy.
Przełamał się wreszcie atak. Ogólnie po tym spotkaniu trzeba pochwalić
całą drużynę.
<p>Kolejne spotkanie już w najbliższą niedzielę 22 maja o godzinie
16:00. Drużynę Rolnika czeka wyjazd do Radoszewic. Miejmy nadzieję że
rywal będzie kolejnym po Kiełczygłowie któremu Rolnik utrudnił walkę o
awans. <br>
