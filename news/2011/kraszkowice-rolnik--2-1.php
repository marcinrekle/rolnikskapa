<p class="wprowadzenie">Kiepskiego początku Rolnika w nowym sezonie ciąg 
dalszy. Podobnie jak tydzień wcześniej Rolnik przegrywa 1:2. Chociaż tym 
razem to my prowadziliśmy a przeciwnik musiał gonić wynik. Najwiekszy wpływ 
na wynik miały niewykorzystane sytuacje, w tym rzut karny, oraz czerwone 
kartki dla dwóch zawodników Rolnika. Bramkę dla naszego zespołu zdobył 
Adam Pazera.  

<p>Od samego początku lepiej prezentowali się gospodarze, którzy dobrze
weszli w mecz. Oddawali dosyc dużo strzałów na naszą bramkę, ale albo były 
niecelne, albo bronił pewnie <b>Radek Kurzynoga</b>. Z biegiem czasu gra się 
wyrównywała i coraz częściej Rolnik gościł pod bramką Kraszkowic. Niestety 
nasi zawodnicy podejmowali złe decyzje, uderzali niecelnie badź przeszkadzało 
boisko. Po odbiciu od twardej nawierzchni nie było wiadomo gdzie piłka może 
się za chwilę znaleźć. Pierwszą dobrą sytuacje zmarnował <b>Przemek Mielczarek</b>, 
który wszedł między dwóch rywali, ale zamiast jeszcze trochę podciągnąc 
zdecydował się na strzał. Uderzenie napastnika nie zagroziło bramce gospodarzy. 
W kolejnej sytuacji już nie decydował się na strzał, tylko dograł sprzed 
końcowej lini na pole bramkowe Kraszkowic. Doskonała sytuację zmarnował 
<b>Łukasz Łyp</b>. Ten sam zawodnik mógł zdobyć bramkę strzałem głową ale 
minimalnie przestrzelił. Bardzo blisko zdobycia bramki był także <b>Damian Lesiak</b>, 
jednak jego uderzenie z rzutu wolnego minimalnie minęło okienko bramki. 
Mimo kilku zmarnowanych szans Rolnikowi udało się objąć prowadzenie. Piłkę 
przejął <b>Przemek Mielczarek</b> i zagrał do <b>Damiana Rydza</b>, który 
podał do <b>Adama Pazery</b>. Lewoskrzydłowy Rolnika poczekał aż bramkarz 
się położy i strzałem nad nim umieścił piłkę w siatce. kolejne sytuacje nie 
były już klarowne.  



<p>Gospodarze od początku drugiej połowy starali się odrobić jednobramkową 
stratę. Jednak to Rolnik był bliżej podwyższenia prowadzenia, niż Kraszkowice 
wyrównania. Niestety po raz kolejny dała o sobie znać nieskuteczność naszej drużyny. 
W polu karnym skaczącego do główki <b>Lukasza Łypa</b> nieprzepisowo powstrzymywał 
zawodnik gospodarzy i sędzia bez wachania wskazał na "wapno". Do jedenastki 
podszedł <b>Kamil Kapela</b>. Strzał naszego obrońcy był w środek bramki, a do tego 
bardzo słaby i bramkarz bez problemu złapał piłkę. Kolejną swietną okazję 
zmarnował <b>Damian Rydz</b>, który po podaniu <b>Przemka Mielczarka</b> znalazł się w 
dogodnej sytuacji ale zbyt daleko wypuścił sobie piłkę i ta padła łupem 
bramkarza Kraszkowic. Pod koniec spotkania niewykorzystane sytuację się zemściły. 
Zaczeło się od dwóch żółtych kartek dla <b>Grzegorza Pawlika</b>(za odkopnięcie piłki 
oraz faul od tyłu na zawodniku Kraszkowic) w ciągu minuty. Następnie z boiska 
wyleciał <b>Damian Rydz</b>(za kłótnie z sędzią i faul). Na kwadrans przed zakończeniem 
meczu Kraszkowice doprowadziły do remisu. Zasłonięty <b>Radek Kurzynoga</b> nie zdołał 
obronić strzału sprzed pola karnego. Ostatnie minuty to "obrona Częstochowy" w 
wykoaniu Rolnika. Niestety nie udało się dowieźć remisu do końca. Kolejny strzał 
sprzed pola karnego wylądował w siatce bramki Rolnika. Na koniec meczu 
Rolnik miał jeszcze szansę na bramkę, ale całkowice nie udał się strzał z rzutu 
wolnego.  

<p>Bardzo kiepsko w tym sezonie Rolnik wystartował w B klasie. Po ledwie dwóch 
kolejkach strata do lidera wynosi już sześć oczek. Co jest juz smutną tradycją 
zawodzi skuteczność. Przez to już drugi mecz przegraliśmy na swoje życzenie. 
nie mozna też zapomnieć o niepotrzebnych osłabieniach przez kartki. Prawie 
wszystkich można było uniknąć bo wynikały raczej z głupoty niż ze złośliwości 
zawodników.   


<p>
<p>Za tydzien Rolnik będzie miał kolejną okazję na pierwsze zwycięstwo w nowym 
sezonie. O godz 14:30 na własnym boisku rozegramy spotkaie z Orionem Olewin. 
to jest już ostatni dzwonek dla drużyny jeśli chcemy o coś walczyć w tym sezonie.
