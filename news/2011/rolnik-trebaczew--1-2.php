<p class="wprowadzenie">Falstart Rolnika w nowym sezonie. Nasza ekipa kiepsko 
rozpoczęła rozgrywki od porażki 1:2 na własne życzenie z drużyną LZS Trębaczew. 
W meczu dużo lepszą ekipą był Rolnik jednak to goście zainkasowali trzy punkty. 
Jedyną bramkę dla naszej drużyny zdobył Kamil Kapela.    

<p>Pierwsze minuty to lekka przewaga gości, jednak nie było zagrożenia pod bramką 
<b>Radosława kurzynogi</b>. Z każdą minutą coraz bardziej rozkręcali się gracze Rolnika. 
Powoli nasza drużyna zaczęła osiągać przewagę. Niestety to goście zdobyli bramkę i to w 
najmniej spodziewanym momencie. Do piłki zmierzającej na aut w okolicach naszego pola karnego 
doszedł <b>Kamil Kapela</b>, cofnął się do lini końcowej i odgrywał w kierunku naszego bramkarza. 
Piłkę przejął zawodnik gości i umieścił ją w siatce. Tak dziwnej bramki Rolnik chyba jeszzce nie stracił. 
Wystarczyło pozwolić piłce wyjść poza boisko lub wybić ją jak najdalej do przodu. 
Ta bramka trochę podłamała zespół ale potrafiliśmy sobie stworzyć kilka dogodnych 
sytuacji. Przy odrobimie szczęścia Rolnik mógł doprowadzić do wyrównania, a nawet objąć
 prowadzenie. Trzykrotnie po strzałach naszej drużyni piłka lądowała na poprzeczce. Jeden 
 strzał wyszedł poza boisko. W dwóch przypadkach piłka odbijała się od lini bramkowej 
 i wracała przed bramkę. W bardzo dobrej sytuacji znajdujący się przed bramką 
 <b>Grzegorz Pawlik</b> zbyt długo zwlekał ze strzałem i został zablokowany. 
 Także blisko był <b>Łukasz Łyp</b>, lecz jego strzał głową był minimalnie niecelny. 
 Pierwsza odsłona skończyła się prowadzeniem gości. 



<p>Cała druga połowa to nieustanne ataki Rolnika i kontry Trębaczewa. 
Po jednej z nich zawodnicy gości podwyższyli prowadzenie na 2:0. 
Rolnik wywalczył aut na wysokości pola karnego gości. Piłki w rekach nie zdołał 
utrzymać bramkarz Trębaczewa i gola zdobył <b>Kamil Kapela</b>. Mimo kilku sytuacji 
bramkowych Rolnikowi nie udało się wywalczyć korzystnego rezulatatu.

<p>Nowy sezon rozpoczął od porażki, mimo że był zespołem lepszym. Niestety po raz 
kolejny dała o sobie znać bardzo kiepska skuteczność. W kilku sytuacjach brakowało 
szczęścia w innych zaś zimnej krwi bądź dokładności. Napewno drużynie nie można 
odmówic walki do samego końca o korzystny rezultat. Na minus oprócz marnowania 
sytuacji trzeba zaliczyć dwie głupie żółte kartki dla Dawida Taranka na sekundy 
przed zakończeniem spotkania.


<p>
<p>Za tydzień Rolnik pojedzie na pierwszy wyjazd w nowym sezonie. Uda się 
do Kraszkowic gdzie o godzinie 15:00 zmierzy się z miejscową drużyną.
